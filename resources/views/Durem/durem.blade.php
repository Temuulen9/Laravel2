<h3>Арван долоо. Төмөр замын гарам нэвтрэх</h3>

17.1. Жолооч төмөр замыг зөвхөн гарамтай хэсгээр нэвтэрнэ. Ингэхдээ ойртон ирсэн галт тэрэг (зүтгүүр)-ийг урьдчилан өнгөрүүлнэ.</br>

17.2. Төмөр замын гарамд ойртон ирсэн жолооч хурдаа хасах ба зайлшгүй тохиолдолд гармын өмнө зогсож, аюулгүй байдал хангагдсан эсэхийг магадлан, гэрэл болон дуут дохио (хонх), тэмдэг, тэмдэглэлийн заалт, хаалтын байрлал, гармын жижүүрийн заалтыг удирдлага болгоно.</br>

17.3. Төмөр замын гармын хаалт хаалттай буюу хаагдаж эхэлж байвал, мөн анивчсан улаан гэрэл ассан, эсхүл дуут дохио (хонх) ажиллаж байхад, түүнчлэн гармын жижүүр хориглосон дохио өгөх үед гарам руу орохыг хориглоно.</br>

Тайлбар: Гармын жижүүр зогсох дохиог өгөхдөө тээврийн хэрэгслийн ирэх зүгт ар буюу өвөр талаараа хандан дохиур, улаан гэрэл буюу дарцаг өргөх, эсхүл гараа алдалж дохих буюу чанга яригч хэрэгслээр зогсохыг шаардаж анхааруулна.</br>

17.4. Үзэгдэх хүрээнд галт тэрэг ойртон ирж байвал гармын жижүүр, хаалт, гэрэл буюу дуут дохио ажиллаж байгаа эсэхээс үл хамааран гарам руу орохыг хориглоно.</br>

17.5. Төмөр замын гармаар нэвтрэх хөдөлгөөн хориглогдсон үед жолооч «Зогс-шугам» (1.11 тэмдэглэл ба 5.32 тэмдэг) буюу 3.5 тэмдэг, эсхүл гэрлэн дохионы өмнө, хэрэв эдгээр нь байхгүй бол хаалтад 5 м-ээс ойргүй зайд, хаалтгүй тохиолдолд төмөр зам хүртэл 10 м-ээс багагүй зайд зогсоно.</br>

17.6. Жолоочид дараахь зүйлийг хориглоно:</br>

        а/ гармын хаалтыг дур мэдэн нээх;</br>

        б/ гармын хаалтыг болон гармын өмнө зогсож байгаа тээврийн хэрэгслийг эсрэг урсгалаар тойрон гарч нэвтрэх;</br>

        в/ гармыг гэмтээж болзошгүй машин механизмыг тээвэрлэхээс бусад байдлаар гаргах;</br>

        г/ цагт 8 км-ээс бага хурдтай явдаг машин механизм болон энэ дүрмийн 24.5-д заасан овор хэмжээтэй буюу хүнд ачаатай тээврийн хэрэгслийг тухайн төмөр замын гарам хариуцсан суурь бүтэц эзэмшигчийн зөвшөөрөлгүйгээр гармаар гаргах;</br>

        д/ гарам дээр тээврийн хэрэгслээ зогсоож болзошгүй түгжрэл, саатал тохиолдсон үед гарам руу орох.</br>

17.7. Төмөр замын гарам дээр жолооч зайлшгүй зогсолт хийвэл зорчигчдыг нэн даруй буулган, ослын дохионы гэрлээ асааж, гармыг чөлөөлөх арга хэмжээ авна. Хэрэв гармыг нэн даруй чөлөөлөх боломжгүй бол:</br>

        а/ бололцоотой бол хоёр хүнийг төмөр замын дагуу гармаас хоёр тийш (ганц хүнтэй бол түүнийг замын байдал муу үзэгдэх тал руу) 1000 м-ийн зайд явуулж галт тэргэнд зогсох дохио өгүүлнэ. Ингэхдээ тэр хүмүүст дохио өгөх аргыг зааварчилна;</br>

        б/ жолооч тээврийн хэрэгслийн дэргэд үлдэж түгшүүрийн дуут дохио өгнө;</br>

        в/ гармыг чөлөөлөх талаар бусдаас бололцоотой тусламжийг авна;</br>

        г/ хэрэв галт тэрэг ойртон ирж байвал угтан гүйж зогсох дохио өгнө.</br>

        Тайлбар: Түгшүүрийн дуут дохиог нэг урт, гурван богино хэмнэлээр, зогсох дохиог өдрийн цагт тод өнгийн сайн харагдах зүйлээр, харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд гэрэл буюу галаар тойруулан даллаж өгнө.</br>
