<h3> Арван нэг. Тээврийн хэрэгсэл байрлан явах </h3>

11.1. Эгнээний тоог зорчих хэсэг дээрх тэмдэглэл болон 5.10.а, 5.10.б, 5.11.а, 5.11.б тэмдгээр тодорхойлно. Хэрэв эдгээр тэмдэг, тэмдэглэл байхгүй бол жолооч харааны баримжаагаар тухайн зорчих хэсгийн өргөн, автомашины өргөний хэмжээ, хажуугийн аюулгүйн зайг харгалзан эгнээний тоог тодорхойлох бөгөөд тухайн зорчих хэсэг эсрэг хөдөлгөөнтэй бол түүний өргөний зүүн гар тал дахь тэн хагасыг эсрэг хөдөлгөөнд зориулагдсанд тооцно.</br>

11.2. Эгнээний хилийг тэмдэглэсэн зорчих хэсэг дээр жолооч тээврийн хэрэгслээрээ тухайн эгнээг голлож явна. Байр эзлэхийн тулд тэмдэглэлийг давахдаа энэ дүрмийн 2 дугаар хавсралтын 1.4, 1.5 заалтыг баримтална. Хоёр дугуйт мотоциклын жолооч эгнээг голлон дангаараа эзэлж явах эрхтэйгээс гадна мөн нэг эгнээнд хоёроос илүүгүй мотоцикл зэрэгцэн явж болно.</br>

11.3. Эсрэг хөдөлгөөнтэй хоёр эгнээгээр зорчдог замд гүйцэж түрүүлэх буюу саадыг тойрон гарахаас бусад тохиолдолд эсрэг урсгал сөрөхийг хориглоно.</br>

11.4. Эсрэг хөдөлгөөнтэй гурван эгнээгээр зорчдог замын дунд эгнээ сөрөг хөдөлгөөнтэй бол уг эгнээнд зөвхөн гүйцэж түрүүлэх, саадыг тойрон гарах, зүүн гар тийш буюу буцаж эргэхийн тулд орохыг зөвшөөрөх бөгөөд түүний зүүн гар талын эгнээнд урсгал сөрөхийг хориглоно.</br>
11.5. Эсрэг хөдөлгөөнтэй дөрөв буюу түүнээс олон эгнээгээр зорчдог замд эсрэг урсгал сөрөхийг хориглоно.

11.6. Суурин газрын гаднах замд болон 5.1, 5.5 тэмдэг тавигдсан замд тээврийн хэрэгсэл зорчих хэсгийн баруун гар талын захад аль болох ойр явах бөгөөд баруун гар талын зөвшөөрөгдсөн эгнээ чөлөөтэй байхад (зүүн гар тийш буюу буцаж эргэхийн тулд байр эзлэхээс бусад тохиолдолд) түүний зүүн гар талын аль ч эгнээгээр явахыг хориглоно.</br>

11.7. Суурин газарт жолооч энэ дүрмийн 11.8–11.12, 25.4, 25.6, 25.7, 26.3-т заасан шаардлагыг зөрчихгүйгээр аль тохиромжтой эгнээгээ сонгож явна. Хөдөлгөөний эрчим ихтэй, эгнээ чөлөөгүй замд эргэх үйлдэл хийх, саадыг тойрон гарах, зогсохоос бусад тохиолдолд эгнээ байр өөрчлөхийг хориглоно.</br>

11.8. Нэг чигт гурав буюу түүнээс олон эгнээгээр зорчдог замын тухайн чигийн хамгийн зүүн гар талын эгнээнд зөвхөн зүүн гар тийш буюу буцаж эргэхийн тулд байр эзлэх, мөн хөдөлгөөний эрчим ихтэй, бусад эгнээ чөлөөгүй үед байрлан явахыг зөвшөөрнө. Харин бүх жин нь 3.5 тонноос их ачааны автомашин зөвхөн зүүн гар тийш буюу буцаж эргэхийн тулд байр эзлэхээс бусад тохиолдолд уг эгнээгээр байрлан явахыг хориглоно. Нэг чигийн хөдөлгөөнтэй замын хувьд түр зогсохын тулд хамгийн зүүн гар талын эгнээнд орохыг зөвшөөрнө.</br>

11.9. Чиглэлийн тээврийн хэрэгслийн эгнээ бүхий замд чиглэлийн тээврийн хэрэгсэл нь энэ дүрмийн 10.5-д заасны дагуу байр эзлэх, эсхүл саадыг тойрон гарахаас бусад тохиолдолд зөвхөн уг эгнээгээр явна.</br>

11.10. Чиглэлийн тээврийн хэрэгслийн эгнээ бүхий замд чиглэлийн тээврийн хэрэгслээс бусад тээврийн хэрэгсэл энэ дүрмийн 10.5-д заасны дагуу байр эзлэхээс бусад тохиолдолд уг эгнээгээр байрлан явахыг хориглоно.</br>

Тайлбар: Дээрх заалт нь суудлын такси болон «Хүүхэд тээвэрлэж яваа тээврийн хэрэгсэл», «Тулгуур эрхтний бэрхшээлтэй хүн» таних тэмдэг бүхий тээврийн хэрэгсэл, мөн энэ дүрмийн 25.4-т заасны дагуу зорчих хэсгийн баруун гар талын захаар байрлан явах унадаг дугуй, мопедод хамаарахгүй.</br>

11.11. Энэ дүрмийн 25.3, 25.5, 26.3-д зааснаас бусад тохиолдолд тээврийн хэрэгслээр явган хүний зам болон хөвөөгөөр явах, мөн механикжсан тээврийн хэрэгсэл буюу ердийн хөсгөөр унадаг дугуйн зам болон унадаг дугуйн эгнээгээр явахыг хориглоно.</br>

11.12. Трактор, өөрөө явдаг машин механизм болон цагт 40 км-ээс илүү хурдтай явах боломжгүй тээврийн хэрэгсэл нь гүйцэх, гүйцэж түрүүлэх, саадыг тойрон гарах, зүүн гар тийш буюу буцаж эргэхийн тулд байр эзлэхээс бусад тохиолдолд зорчих хэсгийн хамгийн баруун гар талын зөвшөөрөгдсөн эгнээгээр явна.</br>

11.13. Эсрэг хөдөлгөөнтэй хоёр эгнээгээр зорчдог замд, түүнчлэн дунд эгнээ нь сөрөг хөдөлгөөнтэй гурван эгнээгээр зорчдог замд цагт 40 км-ээс илүү хурдтай явах боломжгүй, мөн овор ихтэй буюу урт тээврийн хэрэгслийн жолооч нь зорчих хэсгийн баруун гар талын захад аль болох ойр явах бөгөөд араас яваа тээврийн хэрэгсэл өөрийг нь аюулгүй гүйцэж түрүүлэх боломжтой байх хоорондын зайг урд яваа тээврийн хэрэгслээс барьж явна.</br>
Тайлбар: Дээрх заалт нь хөдөлгөөний эрчим, нягтрал ихтэй буюу гүйцэж түрүүлэх хориотой замын хэсэг, мөн цуваанд хамаарахгүй.</br>

11.14. Жолооч нь урдаа яваа тээврийн хэрэгсэл хурдаа хасах буюу зогсоход түүнийг мөргөхгүй, хажуу дахь тээврийн хэрэгслийг шүргэхгүй байх хэмжээний хоорондын болон хажуугийн зайг хөдөлгөөний хурднаас хамааруулан сонгож явна.</br>
