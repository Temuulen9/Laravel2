<h3>Арван хоёр. Тээврийн хэрэгслийн хурд</h3>
12.1. Жолооч хөдөлгөөний эрчим, тээврийн хэрэгсэл болон тээж яваа ачааны онцлог, замын болон цаг агаарын нөхцөл, хөдөлгөөний дагуу үзэгдэлтийг харгалзан тээврийн хэрэгслийн хурдыг энэ дүрэмд тогтоосон хязгаараас хэтрүүлэхгүйгээр тохируулан сонгож явна.</br>

12.2. Харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд жолооч өөрийн үзэгдэх хүрээн дотор тээврийн хэрэгслээ зогсоох боломжтой хурдыг сонгож явна.</br>

12.3. Жолооч хөдөлгөөнд аюул, саад тулгарахыг мэдсэн үед тээврийн хэрэгслийн хурдыг хасаж, зайлшгүй тохиолдолд зогсоох арга хэмжээ авна.</br>

12.4. Тээврийн хэрэгслийн хурдыг суурин газарт цагт 60 км, суурин газрын гадна цагт 80 км, тууш замд цагт 100 км-ээс хэтрүүлэхийг хориглоно.</br>

12.5. Дараахь тохиолдолд тээврийн хэрэгслийн хурдыг дор дурдсанаас хэтрүүлэхгүйгээр сонгож явна:</br>

        а/ автобус, троллейбусаар хүн тээвэрлэх үед суурин газарт цагт 50 км, суурин газрын гадна цагт 70 км, тууш замд цагт 80 км;</br>

        б/ мотоцикл, чиргүүлтэй автомашин суурин газрын гадна цагт 60 км, тууш замд цагт 80 км;</br>

        в/ хүүхдийг зохион байгуулалттай тээвэрлэх үед цагт 50 км;</br>

        г/ механикжсан тээврийн хэрэгслийг чирэх үед цагт 40 км;</br>

