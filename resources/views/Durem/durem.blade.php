<h3>Арван дөрөв. Түр ба удаан зогсох</h3>

14.1. Жолооч тээврийн хэрэгслийг зам дээр түр ба удаан зогсоохдоо 5.17 буюу 6.12 тэмдэг бүхий зогсоолын талбайг ашиглана. Хэрэв тийм талбай байхгүй бол замын баруун гар талд хөвөөн дээр, хөвөөгүй бол зорчих хэсгийн зах, хашлаганд шахаж, эсхүл энэ дүрмийн 14.3, 14.4-т заасны дагуу явган хүний зам дээр буюу түүний хашлагыг унуулж зогсооно. Мөн энэ байдлаар нэг чигийн хөдөлгөөнтэй замын зүүн гар талд түр зогсохыг зөвшөөрнө.</br>

14.2. Тээврийн хэрэгслийг зорчих хэсгийн захыг дагуулан зогсоох тохиолдолд зөвхөн нэг эгнээгээр цувуулан байрлуулна. Харин хоёр дугуйт тээврийн хэрэгслийг автомашины өргөнөөс илүү гаргахгүйгээр ташуу байрлалаар (зорчих хэсгийн захад өнцөг үүсгэн) буюу хоёроос илүүгүйгээр зэрэгцүүлэн зогсоож болно. Мөн хөвөө болон зорчих хэсгийн өргөсгөсөн талбайд тээврийн хэрэгслийг зорчих хэсэг рүү оруулахгүйгээр ташуу байрлалаар зогсоохыг зөвшөөрнө.</br>

Тайлбар: Зорчих хэсгийн захад зогсож байгаа тээврийн хэрэгслийн харалдаа ард буюу урд нь орж зогсохдоо уг тээврийн хэрэгсэлд хөдөлгөөн эхлэх боломжтой байх хоорондын зайг үлдээнэ.</br>

14.3. Зогсоолд байрлуулах хэлбэр буюу байршлын хилийг 7.6.а-7.6.и нэмэлт тэмдэг буюу 1.1 тэмдэглэлээр заасан байвал уг тэмдэг, тэмдэглэлийн дагуу тээврийн хэрэгслийг байрлуулж зогсооно. 7.6.б, 7.6.в, 7.6.е-7.6.и нэмэлт тэмдэг бүхий зогсоолд суудлын автомашин, мотоцикл, мопед, унадаг дугуйг зорчих хэсэгтэй хиллэсэн явган хүний зам дээр гаргаж (унуулж) зогсоохыг зөвшөөрнө.</br>

14.4. Энэ дүрмийн 14.3-т зааснаас бусад газарт болон мөн 7.4.ж нэмэлт тэмдгээр заасан зориулалтын зогсоол байхгүй газарт унадаг дугуй, мопедыг явган зорчигчийн хөдөлгөөнд саад болгохгүйгээр явган хүний зам дээр зогсоохыг зөвшөөрнө.</br>

14.5. Чиглэлийн тээврийн хэрэгслийн жолооч нь зорчигч буулгах, суулгахдаа буудлын талбайн төгсгөл рүү аль болох ойртож, хашлаганд 0.5 м-ээс илүүгүй зайд шахаж зогсоно.</br>

14.6. Жолоочгүйгээр тээврийн хэрэгсэл үл хөдлөх, өөрөөс нь бусад хүн хөдөлгөх боломжгүй болгох арга хэмжээ авсны дараа тээврийн хэрэгслээс буух, түүнийг орхиж явахыг зөвшөөрнө. Орхиж явахдаа 10 нас хүрээгүй хүүхдийг насанд хүрсэн харгалзах хүнгүйгээр тээврийн хэрэгсэлд үлдээхийг хориглоно.</br>

14.7. Суурин газрын гаднах замд амрах, хонох зэргээр удаан зогсохдоо тээврийн хэрэгслийг тусгай зориулсан талбайд, эсхүл замаас зайдуу газарт зогсооно.</br>

