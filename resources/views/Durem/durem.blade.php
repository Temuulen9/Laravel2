                            <h3> Нэг. Нийтлэг үндэслэл </h3>

                1.1. Энэ дүрмийн зорилго нь Монгол Улсын нутаг дэвсгэрт замын хөдөлгөөний нэгдсэн журам тогтооход оршино. Замын хөдөлгөөнтэй холбоотой бусад хэм хэмжээний актууд нь энэ дүрмийн заалт шаардлагууд дээр үндэслэгдсэн, түүнтэй зөрчилдөхгүй байвал зохино. </br>

                1.2. Энэ дүрэмд хэрэглэсэн дараахь нэр томьёог дор дурдсан утгаар ойлгоно:</br>

                1.2.1. “автобус” гэж жолоочоос гадна наймаас илүү хүний суудалтай, хүн тээвэрлэхэд зориулсан, троллейбусаас бусад автомашиныг;</br>

                1.2.2. “автомашин” гэж мотоцикл, трактор, өөрөө явдаг машин механизмаас бусад механикжсан тээврийн хэрэгслийг;</br>

                1.2.3. “аюултай ачаа” гэж тээвэрлэлтийн үед хүний амь нас, эрүүл мэнд, байгаль орчинд хор хөнөөл тарих, эд хөрөнгийн хохирол учруулж болзошгүй элдэв бодис, түүгээр хийсэн эд зүйл, үйлдвэрийн хаягдал, аж ахуйн зориулалт бүхий бусад зүйл болон тэдгээрийн ариутгаагүй сав, баглаа, боодлыг;</br>

                1.2.4. “байр эзлэх” гэж явж байгаа чигтээ эгнээ байраа солих үйлдлийг;</br>

                1.2.5. “бодит жин” гэж тээврийн хэрэгслийн тухайн үеийн жинг. Чирүүлж яваа тээврийн хэрэгслийн жинг бодит жинд оролцуулан тооцно;</br>

                1.2.6. “бүх жин” гэж тээврийн хэрэгслийн техникийн тодорхойлолтоор тогтоосон өөрийн болон ачаа, жолооч, зорчигчийн нийт зөвшөөрөгдөх дээд жинг. Чиргүүл нь тухайн тээврийн хэрэгслийн бүтцэд хамаарагдах бол бүх жинг чирэгч, чиргүүл хоёрын бүх жингийн нийлбэрээр тооцно;</br>

                1.2.7. “гарц” гэж замын зэргэлдээх нутаг дэвсгэр рүү орох, түүнээс гарахад зориулсан зорчих хэсэг замтай нийлсэн бэлчрийг;</br>

                1.2.8. “гол зам” гэж уулзварт нийлсэн хучилттай болон шороон замын хучилттай нь буюу энэ дүрмийн 1 дүгээр хавсралтын 3.1 «Гол зам», 3.3.а–3.3.в «Туслах зам нийлнэ», 5.1 «Тууш зам» тэмдэг (цаашид тэмдэг, тэмдэглэлийн зөвхөн дугаарыг зааж бичнэ) бүхий замыг;</br>

                1.2.9. “гүйцэж түрүүлэх” гэж явж байгаа эгнээнээсээ гаран эсрэг урсгал сөрж урдаа яваа нэг буюу хэд хэдэн тээврийн хэрэгслийг гүйцэх, буцаж эгнээндээ орох үйлдлийг;</br>

                1.2.10. “гүйцэх” гэж нэг чигт зэрэгцээ эгнээгээр яваа тээврийн хэрэгслээс илүү хурдтай явах үйлдлийг;</br>

                1.2.11. “давуу эрх” гэж бусад замын хөдөлгөөнд оролцогчоос хөдөлгөөнөө урьтан хийх эрхийг;</br>

                1.2.12. “ердийн хөсөг” гэж уналга, ачлагад ашиглаж байгаа мал, амьтан, түүнд хөллөсөн хэрэгсэл (хөсөг)-ийг;</br>

                1.2.13. “жолооч” гэж Замын хөдөлгөөний аюулгүй байдлын тухай хуулийн 3.1.3-т заасныг;</br>

                1.2.14. “зайлшгүй зогсолт” гэж жолооч, зорчигчийн биеийн байдал, тээж яваа ачаа, байгалийн үзэгдлийн улмаас аюул учирч болзошгүй үед, мөн техникийн эвдрэл, зам дээр саад бий болсноос шалтгаалан тээврийн хэрэгслийн хөдөлгөөнийг зогсоосон үйлдлийг;</br>

                1.2.15. “зам” гэж Замын хөдөлгөөний аюулгүй байдлын тухай хуулийн 3.1.4-т заасан болон хөдөлгөөнд нээлттэй хучилттай буюу шороон зурвас газрыг;</br>

                1.2.16. “зам тавьж өгөх” гэж замын хөдөлгөөнд оролцогч давуу эрх бүхий хөдөлгөөнд оролцогчийн хөдөлгөөний хурд болон чигийг өөрчлөхөд нөлөөлөхгүйн тулд хөдөлгөөнөө эхлэхгүй буюу үргэлжлүүлэхгүй байх үйлдлийг;</br>

                1.2.17. “зам тээврийн осол” гэж Замын хөдөлгөөний аюулгүй байдлын тухай хуулийн 3.1.6-д заасныг;</br>

                1.2.18. “замын зэргэлдээх нутаг дэвсгэр” гэж замын ойролцоох зогсоол, талбай, хашаа, хороолол, шатахуун түгээгүүр, үйлдвэр, албан байгууллага зэрэг тээврийн хэрэгслийн дамжин өнгөрөх хөдөлгөөнд зориулагдаагүй нутаг дэвсгэрийг. Замын зэргэлдээх нутаг дэвсгэр дотор энэ дүрмийн заалт, шаардлагыг мөрдөнө;</br>

                1.2.19. “замын хөдөлгөөн” гэж Замын хөдөлгөөний аюулгүй байдлын тухай хуулийн 3.1.5-д заасныг;</br>

                1.2.20. “замын хөдөлгөөнд оролцогч” гэж Замын хөдөлгөөний аюулгүй байдлын тухай хуулийн 3.1.7-д заасныг;</br>

                1.2.21. “зорчих хэсэг” гэж тээврийн хэрэгслийн хөдөлгөөнд зориулсан замын хэсгийг. Зам нь тусгаарлах зурвасаар зааглагдсан хэд хэдэн зорчих хэсэгтэй байж болно;</br>

                1.2.22. “зохицуулагч” гэж замын хөдөлгөөн зохицуулах эрх, үүрэг бүхий хүнийг. Зохицуулагч нь дүрэмт хувцас, ялгах тэмдэгтэй байна;</br>

                1.2.23. “механикжсан тээврийн хэрэгсэл” гэж Замын хөдөлгөөний аюулгүй байдлын тухай хуулийн 3.1.2-т заасныг;</br>

                1.2.24. “мопед” гэж 50 куб.см-ээс ихгүй ажлын багтаамж бүхий дотоод шаталтат хөдөлгүүртэй, эсхүл 0.25 кВт-аас 4 кВт хүртэл чадал бүхий цахилгаан хөдөлгүүртэй, техникийн дээд хурд нь цагт 50 км-ээс ихгүй, хоёр буюу гурван дугуйт тээврийн хэрэгслийг;</br>

                1.2.25. “мотоцикл” гэж хөдөлгүүрийн ажлын багтаамж буюу чадал, эсхүл техникийн дээд хурд нь мопедынхоос илүү, хоёр дугуйт механикжсан тээврийн хэрэгслийг. Энд өөрийн жин нь 400 кг (ачааны зориулалттай бол 550 кг)-аас хэтрэхгүй гурван дугуйт болон мотоциклын төрлийн жолооны залууртай дөрвөн дугуйт механикжсан тээврийн хэрэгсэл мөн хамаарна;</br>

                1.2.26. “саадыг тойрон гарах” гэж тухайн эгнээгээр хөдөлгөөн хэвийн үргэлжлэх боломжийг хязгаарласан хөдөлгөөнгүй биет зүйл (зам тээврийн ослын улмаас зогссон буюу түр ба удаан, эсхүл зайлшгүй зогсолт хийсэн тээврийн хэрэгсэл, мөн зорчих хэсгийн нүх, эвдрэл, гэмтэл, хашсан хэсэг болон зорчих хэсэг дээр тавьсан, унасан эд зүйл гэх мэт)-ийг тээврийн хэрэгслээр тойрон гарах үйлдлийг. Тухайн эгнээнд хөдөлгөөний нягтрал ихэссэнээс шалтгаалж зогссон буюу дүрмийн шаардлагыг биелүүлэхийн тулд зогсож байгаа тээврийн хэрэгслийг саадад тооцохгүй;</br>

                1.2.27. “сургууль орчмын бүс” гэж ерөнхий боловсролын сургуулийн ойролцоох замын хэсгийг хамруулж 5.25 тэмдгээр тэмдэглэсэн нутаг дэвсгэрийг;</br>

                1.2.28. “суурин газар” гэж 5.3, 5.5 тэмдгээр тэмдэглэсэн, хүн ам төвлөрч суурьшсан нутаг дэвсгэрийг;</br>

                1.2.29. “төмөр замын гарам” гэж төмөр замтай нэг түвшинд огтлолцсон замын бэлчрийг. Төмөр замын гармын өргөнийг төмөр замын хоёр талд байрлуулсан гэрлэн дохио буюу хаалт, эдгээр нь байхгүй бол 1.3.а (эсхүл 1.3.б) тэмдгийн хоорондох зайгаар тодорхойлно;</br>

                1.2.30. “тусгаарлах зурвас” гэж зэрэгцээ зорчих хэсгүүдийг хооронд нь тодорхой зайтай салгаж зааглахад зориулагдаж хийгдсэн, тээврийн хэрэгслийн зогсоол болон хөдөлгөөнд зориулаагүй замын хэсгийг;</br>

                1.2.31. “тууш зам” гэж эсрэг хөдөлгөөний зорчих хэсгүүд нь хоорондоо тусгаарлах зурвас, эсхүл замын хашилтаар зааглагдсан бөгөөд бусад зам болон явган хүний зам, унадаг дугуйн зам, мөн төмөр замтай нэг түвшинд огтлолцоогүй, 5.1 тэмдэг бүхий замыг;</br>

                1.2.32. “тээврийн хэрэгсэл” гэж Замын хөдөлгөөний аюулгүй байдлын тухай хуулийн 3.1.1-д заасныг;</br>

                1.2.33. “тээврийн хэрэгслээр зорчигч” гэж тээврийн хэрэгсэл дээр буюу түүний бүхээг дотор байгаа жолоочоос бусад хүнийг. Тээврийн хэрэгсэл рүү орж байгаа болон түүнээс бууж байгаа хүнийг мөн хамааруулж ойлгоно;</br>

                1.2.34. “түр зогсолт” гэж жолооч тээврийн хэрэгслээ 5 минутаас илүүгүй хугацаагаар зориуд зогсоосон үйлдлийг. Зорчигчдыг буулгах, суулгах буюу ачаа ачих, буулгахад үүнээс илүү хугацаагаар зогсоосон үйлдэл нь түр зогсолтод хамаарна;</br>

                1.2.35. “удаан зогсолт” гэж зорчигчдыг буулгах, суулгах буюу ачаа ачих, буулгахаас бусад тохиолдолд жолооч тээврийн хэрэгслээ 5 минутаас дээш хугацаагаар зориуд зогсоосон үйлдлийг;</br>

                1.2.36. “удаан явдалт тээврийн хэрэгсэл” гэж техникийн тодорхойлолтоор тогтоосон дээд хурд нь цагт 30 км-ээс илүүгүй механикжсан тээврийн хэрэгслийг;</br>

                1.2.37. “унадаг дугуй” гэж унаж яваа хүний булчингийн хүчээр явдаг (0.25 кВт-аас илүүгүй чадал бүхий цахилгаан хөдөлгүүрээр тоноглогдсон байж болно), хоёроос цөөнгүй дугуйтай (хөгжлийн бэрхшээлтэй хүний тэргэнцрээс бусад) тээврийн хэрэгслийг;</br>

                1.2.38. “унадаг дугуйн зам” гэж унадаг дугуй, мопедын хөдөлгөөнд зориулсан, 4.4 тэмдэг бүхий зам, замын хэсгийг. Унадаг дугуйн зам нь бусад замаас хийцийн хувьд салангид буюу замын зорчих хэсэг, явган хүний замаас хайс, хашлага, хөвөөгөөр тусгаарлагдсан, эсхүл явган хүний зам дээр явган зорчигчийн хөдөлгөөн явагдах хэсгээс тэмдэглэлээр зааглагдсан буюу гадаргуугийн өнгө, төрлөөр ялгаатай байна;</br>

                1.2.39. “унадаг дугуйн эгнээ” гэж зорчих хэсэг дээрх унадаг дугуй, мопедын хөдөлгөөнд зориулсан, 4.4 тэмдэг бүхий эгнээг. Унадаг дугуйн эгнээ нь зорчих хэсгийн бусад эгнээнээс тэмдэглэлээр зааглагдсан байна;</br>

