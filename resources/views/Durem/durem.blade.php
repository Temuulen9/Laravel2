<h3> Зургаа. Тээврийн хэрэгслээр зорчигчийн үүрэг </h3>

6.1. Тээврийн хэрэгслээр зорчигч дараахь үүргийг хүлээнэ:</br>

        а/ хамгаалах бүсээр тоноглосон суудалд зорчихдоо хамгаалах бүс хэрэглэх (энэ дүрмийн 3.4-ийн “б” заалтын тайлбарт дурдсанаас бусад тохиолдолд);</br>

        б/ мотоцикл, мопедоор зорчихдоо хамгаалах малгайг зааврын дагуу өмсөх;</br>

        в/ тээврийн хэрэгслийг бүрэн гүйцэд зогссоны дараа зориулалтын буух талбай руу, ийм талбай байхгүй бол явган хүний зам, хөвөө тал руу буюу ар тал руу буух, тэр талаас суух.</br>

        Тайлбар: Явган хүний зам, хөвөө тал руу буюу ар тал руу буух, тэр талаас суух боломжгүй бол аюулгүй байдлыг хангаж, бусдын хөдөлгөөнд саад хийхгүйгээр зорчих хэсэг тал руу буух, тэр талаас суухыг зөвшөөрнө.</br>

6.2. Тээврийн хэрэгслээр зорчигчид дараахь зүйлийг хориглоно:</br>

        а/ хөдөлгөөний үед жолоочийн анхаарлыг сарниулах, жолоодлогод саад болох;</br>

        б/ тээврийн хэрэгслийн хүн тээхэд зориулаагүй хэсэг дээр буюу ачааны автомашины тэвшний хашлаганаас дээш гарсан ачаан дээр байрлаж явах, тэвшин дээр зогсож явах, тээврийн хэрэгслээс зүүгдэх;</br>

        в/ тээврийн хэрэгслийн явдал дунд хаалга нээх, буух, суух, ачаа ачих, буулгах, цонхоор биеэ болон бусад зүйл гаргах;</br>

        г/ чиглэлийн тээврийн хэрэгсэл дотор зогсоогоор зорчихдоо зориулалтын бариулаас барихгүй явах;</br>

        д/ зогсож байгаа тээврийн хэрэгслээс зорчих хэсэг талд ачаа буулгах, ачих, тэр талын цонхоор биеэ болон бусад зүйл гаргах;</br>

        Тайлбар: Зорчих хэсэг талд ачаа буулгах, ачих зайлшгүй тохиолдолд аюулгүй байдлыг хангаж, бусдын хөдөлгөөнд саад хийхгүйгээр гүйцэтгэхийг зөвшөөрнө.</br>

        е/ бусдын хөдөлгөөнд саад болохоор байвал зогсож байгаа тээврийн хэрэгслийн хаалгыг нээх.</br>
