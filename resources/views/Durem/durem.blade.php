        <h3> Хорин дөрөв. Ачаа тээвэрлэх </h3>

24.1. Ачааны жин болон тэнхлэгт ноогдох ачааллын хэмжээ нь тухайн тээврийн хэрэгслийн техникийн тодорхойлолтоор тогтоосон хэмжээнээс хэтрэхгүй байна.</br>

24.2. Жолооч хөдөлгөөн эхлэхийн өмнө болон замд явахдаа ачааны татлага, бэхэлгээ, даруулга, байрлал, битүүмжлэлийг сайтар шалгах бөгөөд энэ дүрмийн 24.3-т заасан шаардлага бүрэн хангагдаагүй тохиолдолд хөдөлгөөнөө үргэлжлүүлэхийг хориглоно.</br>

24.3. Ачааг дараах шаардлагыг хангахаар байрлуулж, шаардлагатай бол бэхэлж битүүмжилсэн байна:</br>

        а/ унаж, асгарч, чирэгдэхгүй, хөдөлгөөнд оролцогчдод аюул, саад учруулахгүй;</br>

        б/ жолоочийн харах боломжийг хязгаарлахгүй;</br>

        в/ тээврийн хэрэгслийн тогтворжилтыг алдагдуулахгүй, жолоодлогыг хүндрүүлэхгүй;</br>

        г/ тээврийн хэрэгслийн гадна талын гэрэлтүүлэх хэрэгсэл, гэрэл ойлгуур, эргэх чиг заадаг гэрэл болон гараар өгөх дохио, улсын бүртгэлийн дугаар, таних тэмдгийг халхлахгүй;</br>

        д/ их хэмжээний дуу чимээ, эвгүй үнэр гаргахгүй, тоос шороо босгохгүй, зам ба орчныг бохирдуулахгүй.</br>

24.4. Суурин газарт 22 цагаас 06 цагийн хооронд дуу чимээ гаргаж ачих, буулгахыг хориглоно.</br>

24.5. Ачаатай буюу ачаагүй өндөр нь 4 м-ээс, өргөн нь 2.5 м-ээс, урт нь 20 м-ээс, ачаа нь тээврийн хэрэгслийн овроос арагшаа 2 м-ээс илүү, мөн хоёр чиргүүлтэй, эсхүл хүнд буюу аюултай ачаатай тээврийн хэрэгслээр замын хөдөлгөөнд оролцохдоо эрх бүхий байгууллагаас баталсан тусгай журам, стандартыг баримтална.</br>

Тайлбар: Хоёроос олон чиргүүл чирч буюу хагас чиргүүлийн араас чиргүүл залгаж замын хөдөлгөөнд оролцохыг хориглоно.</br>

24.6. Тээврийн хэрэгслийн овроос уртаашаа 1 м, өргөөшөө 0.4 м-ээс илүү гарсан ачааг тээвэрлэхдээ түүний илүү гарсан хэсэгт «Овор ихтэй ачаа» таних тэмдэг байрлуулахаас гадна харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд урд нь цагаан, ард нь улаан өнгийн гэрэл буюу гэрэл ойлгуур байрлуулна.</br>

24.7. Суудлын автомашин, мотоциклоор түүний овроос уртаашаа буюу өргөөшөө 0.4 м-ээс илүү гарсан ачаа тээвэрлэхийг хориглоно.</br>

