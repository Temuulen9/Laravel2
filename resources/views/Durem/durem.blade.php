        <h3> Хорин зургаа. Ердийн хөсгөөр хөдөлгөөнд оролцох, замаар мал туухад тавих нэмэлт шаардлага </h3>

26.1. Ердийн хөсгийн тоног татлага нь бат бөх байхаас гадна харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд хөдөлгөөнд оролцохдоо баруун, зүүн урд талдаа хоёр цагаан, ар талдаа хоёр улаан гэрэл ойлгууртай (боломжтой бол гэрэлтэй) байна.</br>

26.2. Ердийн хөсгийг 14-өөс дээш насны хүн унаж, хөтөлж (жолоодож) замын хөдөлгөөнд оролцохыг зөвшөөрнө.</br>

26.3. Ердийн хөсгөөр саадыг тойрон гарах, зүүн гар тийш буюу буцаж эргэхийн тулд байр эзлэхээс бусад тохиолдолд зорчих хэсгийн хамгийн баруун гар талын зөвшөөрөгдсөн эгнээгээр явна. Суурин газрын гаднах зам болон 5.5 тэмдэг бүхий замын уулзвар, гарцгүй хэсэгт, явган зорчигчдод саад учруулахааргүй байвал хөвөөгөөр явж болох бөгөөд зам хөндлөн гарах тохиолдолд бусад хөдөлгөөнд оролцогчдод зам тавьж өгнө.</br>

26.4. Суурин газрын гаднах хатуу хучилттай зам болон суурин газрын доторх хөдөлгөөний эрчим ихтэй замд малыг зорчих хэсгийн дагуу тууж явахыг хориглоно. Бусад замд туух тохиолдолд зорчих хэсгийн захыг барин, тээврийн хэрэгсэл зөрж өнгөрөх боломжийг хангана. Уулзвар, гарцаар нэвтрэх тохиолдолд бусад хөдөлгөөнд оролцогчдод урьдаас анхааруулан, тэдэнтэй ойлголцсоны үндсэн дээр, аюулгүй байдлыг хангаж, дүрмийн бус байрлал, дарааллаар нэвтрэхийг зөвшөөрнө.</br>

26.5. Малыг зам хөндлөн буюу гүүрээр тууж гаргахдаа 1.22 тэмдгээр анхааруулсан замын хэсгийг сонгох бөгөөд тийм хэсэг байхгүй бол замын дагуу хоёр тийш сайн харагдах, хайс, хашилтгүй хэсгээр, ойртон ирж яваа тээврийн хэрэгсэлгүй үед гаргана. Ингэхдээ аль болох түргэн, бөөнөөр нь гаргах бөгөөд гүйцэд гаргаж амжаагүй байхад ойртон ирсэн тээврийн хэрэгслийн жолооч зам тавьж өгнө.</br>

26.6. Харанхуй үед замын гэрэлтүүлэггүй хэсэгт болон үзэгдэлт хангалтгүй нөхцөлд малыг замын дагуу тууж явахыг хориглоно. Харин зам хөндлөн тууж гаргах зайлшгүй тохиолдолд замын дагуу хоёр тийш тээврийн хэрэгсэл ирэх зүгт гэрэл (дэнлүү) буюу гэрэл ойлгуур барьсан хүн хашиж гаргана.</br>

26.7. Төмөр замын гармаар ердийн хөсгийг гурваас илүүгүйгээр цувуулж гаргана. Мал тууж гаргахдаа зориулалтын гармаар аюулгүй байдлыг хангаж, богино хугацаанд амжиж гарахуйц тоогоор хэсэг хэсгээр цувуулж гаргана. Харин харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд, эсхүл малын тоо 500-аас дээш бол гармын арчилгаа, үйлчилгээ хариуцсан байгууллага, ажилтанд урьдчилан мэдэгдэж, тэдний зөвшөөрөл, зааврын дагуу гаргана.</br>

26.8. Ердийн хөсгөөр хөдөлгөөнд оролцох, замаар мал туухад дараахь зүйлийг хориглоно:</br>

        а/ уналга, эдэлгээнд гүйцэд сургаагүй (номхроогүй) ердийн хөсгөөр замын хөдөлгөөнд оролцох;</br>

        б/ ердийн хөсгийн овроос уртаашаа буюу өргөөшөө 0.4 м-ээс илүү гарсан ачаа тээвэрлэх;</br>

        в/ суурин газрын доторх замд ердийн хөсгийг давхиулах;</br>

        г/ согтуурсан буюу мансуурсан үедээ замаар мал туух;</br>

        д/ зам дээр ердийн хөсөг, малыг хараа хяналт, эзэнгүй орхих.</br>
