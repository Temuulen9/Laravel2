<h3>Арван зургаа. Явган хүний гарц нэвтрэх</h3>

16.1. Явган хүний зохицуулдаггүй гарц руу ойртон ирсэн жолооч хурдаа хасаж, уг гарцаар гарч байгаа болон гарахаар завдаж байгаа явган зорчигчид зогсож зам тавьж өгнө.</br>

Тайлбар: Явган хүний зохицуулдаг ба зохицуулдаггүй гарцыг энэ дүрмийн 15.3-т заасан зохицуулдаг ба зохицуулдаггүй уулзварын тодорхойлолттой адилтгаж ойлгоно.</br>

16.2. Зөвшөөрсөн гэрэл дохио асах үед жолооч явган хүний гарцыг нэвтрэхдээ зорчих хэсгийг хөндлөн гарч амжаагүй яваа явган зорчигчид зам тавьж өгнө.</br>

16.3. Явган хүний гарцын өмнө тээврийн хэрэгсэл зогссон буюу хурдаа хасаж байвал зэрэгцээ эгнээнд араас нь ирсэн жолооч мөн хурдаа хасах буюу зогсож, энэ дүрмийн 16.1, 16.2-т заасныг баримталж хөдөлгөөнөө үргэлжлүүлнэ.</br>

16.4. Жолооч замын аль ч хэсэгт цагаан таяг барьсан харааны бэрхшээлтэй хүнийг урьдчилан өнгөрүүлнэ.</br>

16.5. «Хүүхэд тээвэрлэж яваа тээврийн хэрэгсэл» таних тэмдэг бүхий автобус ослын дохионы гэрлээ асааж зогссон байвал (замын аль хэсэгт зогссон байгаагаас үл хамааран) ойртон ирсэн жолооч хурдаа хасаж анхаарал болгоомжтой явах бөгөөд хүүхдүүд зам хөндлөн гарч байвал зогсож урьдчилан өнгөрүүлнэ.</br>

16.6. Жолооч явган хүний гарц дээр тээврийн хэрэгслээ зогсоож болзошгүй түгжрэл, саатал тохиолдсон үед (зөвшөөрсөн гэрэл дохио асаж байгаа эсэхээс үл хамааран) гарц руу орохыг хориглоно.</br>

