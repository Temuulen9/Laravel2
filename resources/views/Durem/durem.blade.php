        <h3> Хорин гурав. Хүн тээвэрлэх </h3>

23.1. Зорчигчийг хүн тээвэрлэх зориулалт бүхий тээврийн хэрэгслээр тээвэрлэнэ. Ачааны автомашины жолоочийн бүхээг дотор зориулалтын суудалд зорчигч тээвэрлэхийг зөвшөөрөх бөгөөд харин түүний тэвшин дээр энэ дүрмийн 23.2, 23.3-т зааснаас бусад тохиолдолд хүн тээвэрлэхийг хориглоно.</br>

23.2. Ачааны автомашины тэвшинд ачааг дагалдан буюу хүлээж авахаар яваа хүмүүсийг тэвшний хашлаганы дээд ирмэгээс нам зассан суудалд суулгаж тээвэрлэхийг зөвшөөрнө. Ингэхдээ жолооч хөдөлгөөн эхлэхийн өмнө аюулгүй байдал хангагдсан эсэхийг магадлан, уг хүмүүст буух, суух, зорчих журмыг зааварчилж, хөдөлгөөний үед тэвшин дээр зогсож явахыг хориглох тухай анхааруулбал зохино.</br>

23.3. Батлан хамгаалах, хууль сахиулах, хил хамгаалах, онцгой байдлын байгууллага нь өөрийн дотоодын хэрэгцээнд ачааны автомашины тэвшин дотор зорчигчийн суудал засаж, шат, бүрхүүл гэх мэт хэрэгслээр нэмж тоноглон хүн тээвэрлэх зориулалтаар ашиглаж болно. Ийм тээврийн хэрэгсэлд тавих нэмэлт шаардлага, замын хөдөлгөөнд оролцоход баримтлах зааврыг энэ дүрэм болон холбогдох журам, стандартад нийцүүлэн тогтоож мөрдөнө.</br>

23.4. Жолооч нь тээврийн хэрэгслээ гүйцэд зогсоосны дараа зорчигчийг буулгах буюу суулгах, хаалгаа гүйцэд хаасны дараа хөдөлгөөнөө эхлэх үүрэгтэй. Хаалгыг жолоочийн суудлаас удирддаг бол гүйцэд зогсоогүй байхдаа хаалга нээхийг хориглоно.</br>

23.5. Зорчигчийг суурин газрын доторх чиглэлийн тээврийн хэрэгслээр тээвэрлэхээс бусад тохиолдолд зогсоогоор нь тээвэрлэхийг хориглоно.</br>

23.6. Хүүхдийг зохион байгуулалттай тээвэрлэхэд зөвхөн автобус ашиглах бөгөөд автобусны урд болон ард «Хүүхэд тээвэрлэж яваа тээврийн хэрэгсэл» таних тэмдгийг байрлуулна. Байнгын гэрээний дагуу сургуулийн хүүхэд тээвэрлэх автобус нь MNS 6440 стандартын шаардлагыг хангасан байна.</br>

23.7. Хүүхдийг зохион байгуулалттай тээвэрлэх үед, мөн хот хоорондын болон аялал, жуулчлалаар явах автобусанд тээвэрлэх зорчигчдын тоо нь үйлдвэрээс тавьсан суудлын тооноос илүүгүй байх бөгөөд суудал бүр нь хамгаалах бүсээр тоноглогдсон байна.</br>

23.8. Суудлын автомашинд болон ачааны автомашины жолоочийн бүхээгт 10 хүртэлх насны хүүхдийг тээвэрлэхдээ биеийн жин, өсөлтөд нь тохирсон зориулалтын нэмэгдэл суудал буюу хамгаалах хэрэгсэл ашиглана. Хүүхдийн нэмэгдэл суудал, хамгаалах хэрэгсэл нь MNS ISO 13216 (хүүхдийн хамгаалах систем) стандартын шаардлагад нийцсэн байх бөгөөд түүнийг тээврийн хэрэгсэлд байрлуулж ашиглахдаа үйлдвэрлэгчээс тогтоосон зааврыг баримтална.</br>

Тайлбар: Суурин газарт суудлын таксигаар хүүхдийг тээвэрлэх тохиолдолд дээрх шаардлагыг биелүүлэх боломжгүй бол насанд хүрсэн хүн өвөр дээрээ тэвэрч зөвхөн арын суудалд авч явахыг зөвшөөрнө. Мөн хүүхдийн өндөр 140 см-ээс дээш бол суудлын автомашины арын суудалд зорчигчийн хамгаалах бүс зүүлгэн тээвэрлэхийг зөвшөөрнө.</br>

23.9. Дараахь тохиолдолд хүн тээвэрлэхийг хориглоно:</br>

        а/ тээврийн хэрэгслийн техникийн тодорхойлолтоор тогтоосон тооноос олон зорчигч;</br>

        б/ тээврийн хэрэгслийн хүн тээхэд зориулаагүй хэсэгт (энэ дүрмийн 23.2, 23.3-т зааснаас бусад тохиолдолд);</br>

        в/ трактор болон өөрөө явдаг машин, механизмд;</br>

        г/ ачааны болон аяллын чиргүүлд;</br>

        д/ ачааны мотоциклын тэвшинд;</br>

        е/ 10 хүртэлх насны хүүхдийг мотоциклын арын суудалд;</br>

       ж/ жолоочийн өвөр дээр, эсхүл энэ дүрмийн 23.8-ын тайлбарт дурдсанаас бусад тохиолдолд суудлын автомашинд болон ачааны автомашины жолоочийн бүхээгт зорчигчийн өвөр дээр;</br>

