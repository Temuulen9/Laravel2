<h3> Арав. Хөдөлгөөн эхлэх болон чиг өөрчлөх </h3>

10.1. Жолооч хөдөлгөөн эхлэхийн өмнө зам, орчны байдлыг биеэр шалгаж, хөдөлгөөн эхлэх, чиг өөрчлөх, байр эзлэх, зогсохдоо аюул, осол үүсгэхгүй байх бүх нөхцөлийг хангана.</br>

10.2. Жолооч хөдөлгөөн эхлэхдээ бусад оролцогчдод зам тавьж өгнө.</br>

10.3. Жолооч гарцаар замд нийлэх, замаас гарахдаа уг замын дагуу яваа хөдөлгөөнд оролцогчдод зам тавьж өгнө.</br>

10.4. Жолооч байр эзлэхийн өмнө зэрэгцээ эгнээнд чигээрээ яваа тээврийн хэрэгсэлд зам тавьж өгнө. Харин харилцан байраа солиход баруун гар талд яваа нь эхэлж байр эзэлнэ.</br>

10.5. Уулзвар, гарцаар нэвтрэх хөдөлгөөн нь зөвхөн нэг чигт зөвшөөрөгдсөнөөс бусад тохиолдолд жолооч баруун гар тийш эргэхийн өмнө хамгийн баруун, зүүн гар тийш буюу буцаж эргэхийн өмнө хөдөлгөөний тухайн чигийн хамгийн зүүн гар талын захад урьдчилан байр эзэлсэн байна.</br>

Хэрэв тухайн эгнээнээс явах хөдөлгөөний чигийг 5.10.а, 5.10.б тэмдэг,      1.17 тэмдэглэлээр заасан байвал уг тэмдэг, тэмдэглэлийн заалтыг баримтална.</br>
10.6. Жолооч баруун буюу зүүн гар тийш эргэхдээ тээврийн хэрэгслийн оврын хэмжээ болон бусад шалтгаан /ачааны онцлог, зам дээрх саад гэх мэт/-аар энэ дүрмийн 10.5-д заасан шаардлагыг биелүүлэх боломжгүй бол аюулгүй байдлыг хангаж, бусад тээврийн хэрэгслийн хөдөлгөөнийг саатуулахгүйгээр уг үйлдлийг өөр эгнээнээс гүйцэтгэхийг зөвшөөрнө.</br>

Уулзвар, гарцаас бусад замын хэсэг дээр хөдөлгөөний тухайн чигийн зүүн гар талын захаас буцаж эргэхэд зорчих хэсгийн өргөн хүрэлцэхгүй байвал уг үйлдлийг баруун гар талын эгнээ буюу хөвөөнөөс гүйцэтгэхийг зөвшөөрнө. Ингэхдээ өөдөөс буюу зэрэгцээ эгнээнд араас яваа тээврийн хэрэгсэлд зам тавьж өгнө.</br>

10.7. Эгнээнээс явах чигийг 5.10.а, 5.10.б тэмдэг, 1.17 тэмдэглэлээр заагаагүй уулзварын өмнө, улаан гэрэлтэй хамт ассан нэмэгдэл гэрэл дохионы сум заасан чигт явах захын эгнээг эзэлсэн жолооч араасаа тухайн чигт явах тээврийн хэрэгсэл ирвэл өөрийн явах чигийг үл харгалзан нэмэгдэл гэрэл дохионы сум заасан чигт хөдөлгөөнөө үргэлжлүүлнэ.</br>

10.8. Жолооч уулзвар, гарцаар нэвтрэхдээ тухайн эгнээнээс өөрийн явах чигийн дагуу хөдөлгөөний эгнээний үргэлжлэлийг заасан 1.7 тэмдэглэлийг баримтална. Хэрэв тийм тэмдэглэл байхгүй бол дараах байдлаар нэвтэрнэ:</br>

        а/ уулзвар, гарцаас гарахдаа тухайн орж буй зорчих хэсгийн эсрэг урсгалыг сөрөхгүйгээр үйлдлийг гүйцэтгэнэ;</br>

        б/ зүүн гар тийш буюу буцаж эргэхдээ өөдөөс мөн зүүн гар тийш буюу буцаж эргэх тээврийн хэрэгслийг баруун гар талаараа өнгөрөөж байхаар үйлдлийг гүйцэтгэнэ;</br>

        в/ баруун гар тийш нэвтрэх үйлдлийг зорчих хэсгийн баруун гар талын захад аль болох ойр явж гүйцэтгэнэ;</br>

        г/ тухайн уулзвар, гарцаар нэвтрэх хөдөлгөөн зөвхөн аль нэг чигт зөвшөөрөгдсөн, эсхүл баруун буюу зүүн гар тийш эргэх үйлдлийг 5.10.а, 5.10.б тэмдэг буюу 1.17 тэмдэглэлээр олон эгнээнээс давхар гүйцэтгэхийг зөвшөөрсөн байвал зэрэгцэн эргэх тээврийн хэрэгслүүд эзэлж яваа байрлалаа солихгүйгээр хажуугийн зайгаа барьж нэвтэрнэ. Ингэхдээ эргэлтийн дотор талд яваа тээврийн хэрэгслийн жолооч аль болох богино зайг туулж нэвтэрнэ.</br>

10.9. Уулзвараас бусад газарт жолооч зүүн гар тийш буюу буцаж эргэхдээ өөдөөс чигээрээ яваа болон баруун гар тийш эргэх тээврийн хэрэгсэлд зам тавьж өгнө.</br>

10.10. Хөдөлгөөний дараалал нь энэ дүрмийн бусад заалтуудад тусгагдаагүй газарт жолооч баруун гар талаас ойртон ирсэн тээврийн хэрэгсэлд зам тавьж өгнө.</br>

10.11. Хурд хасах эгнээтэй замаас гарахдаа жолооч уг эгнээнд орсны дараа тээврийн хэрэгслийн хурдыг хасна. Харин хурд авах эгнээгээр явж замд нийлэхдээ тухайн замаар яваа тээврийн хэрэгсэлд зам тавьж өгнө.</br>

10.12. Өөдөөс яваа тээврийн хэрэгсэлтэй зөрж өнгөрөхөд хүндрэлтэй буюу боломжгүй байвал саадыг тойрон гарах тээврийн хэрэгслийн жолооч зам тавьж өгнө. Харин 1.10 буюу 1.11 тэмдгээр анхааруулсан уруу буюу өгсүүр замд өгсөж яваа тээврийн хэрэгсэлд зам тавьж өгнө.</br>

10.13. Дараахь газруудад буцаж эргэхийг хориглоно:</br>

        а/ явган хүний гарц дээр;</br>

        б/ унадаг дугуйн зам (эгнээ)-ын огтлолцол дээр;</br>

        в/ чиглэлийн тээврийн хэрэгслийн буудал дээр;</br>

        г/ төмөр замын гарам дээр;</br>

        д/ гүүрэн дээр болон доор;</br>

        е/ хонгил дотор;</br>

        ж/ үзэгдэлт замын дагуу аль нэг чигт 100 м-ийн дотор хязгаарлагдмал газарт.</br>

10.14. Бусдад саад учруулахааргүй газарт, аюулгүй байдлыг хангасан нөхцөлд ухрах үйлдэл хийхийг зөвшөөрнө. Шаардлагатай тохиолдолд ухрах үйлдлийг бусдын тусламжтайгаар гүйцэтгэх бөгөөд жолооч аюулгүй байдлыг хангах үүргээс чөлөөлөгдөхгүй.</br>

10.15. Уулзвар дээр болон энэ дүрмийн 10.13-т заасан буцаж эргэхийг хориглосон бүх газарт ухрах хөдөлгөөнийг хориглоно.</br>
