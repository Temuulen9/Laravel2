<h3> Ес. Анхааруулах дохио ба таних тэмдэг </h3>

9.1. Анхааруулах дохио ба тээврийн хэрэгслийн таних тэмдгийг жолооч өөрийн хийж буй үйлдэл, тээврийн хэрэгслийнхээ онцлогийг бусад хөдөлгөөнд оролцогчдод мэдэгдэж, аюул, осол үүсгэхээс урьдчилан сэргийлэх зорилгоор энэ дүрэмд заасны дагуу хэрэглэнэ.</br>

9.2. Анхааруулах дохио болон таних тэмдэг нь давуу эрх олгохгүй бөгөөд урьдчилан сэргийлэх зайлшгүй арга хэмжээ авахаас чөлөөлөхгүй.</br>

I. Ослын дохионы гэрэл</br>

9.3. Ослын дохионы гэрлийг дараахь тохиолдолд хэрэглэнэ:</br>

        а/ зам тээврийн ослын улмаас зогссон үед;</br>

        б/ түр зогсох хориотой газарт зайлшгүй зогсолт хийсэн тохиолдолд;</br>

        в/ харанхуй үед замын гэрэлтүүлэггүй хэсэгт болон үзэгдэлт хангалтгүй нөхцөлд оврын гэрэл нь асахгүй (эсхүл байхгүй) тээврийн хэрэгслийг замаас гаргах боломжгүйн улмаас зорчих хэсэг, хөвөөн дээр зогсоосон тохиолдолд;</br>

        г/ «Хүүхэд тээвэрлэж яваа тээврийн хэрэгсэл» таних тэмдэг бүхий автобусаас хүүхэд буулгах, түүнд суулгах үед;</br>

        д/ жолоочийн нүд гялбан зорчих баримжаа алдах үед;</br>

        е/ механикжсан тээврийн хэрэгсэл чирч яваа үед (чирч ба чирүүлж яваа);</br>

        ж/ тээврийн хэрэгсэл нь аюул учруулж болзошгүй бусад тохиолдолд.</br>

II. Ослын зогсолтын тэмдэг</br>

9.4. Жолооч энэ дүрмийн 9.3-т заасны дагуу ослын дохионы гэрлээ асааж зогсох шаардлагатай үед уг гэрэл нь ажиллахгүй (эсхүл байхгүй) бол ослын зогсолтын тэмдгийг нэн даруй тавьж анхааруулахаас гадна мөн дараах тохиолдолд (ослын дохионы гэрэл асааснаас үл хамааран) хэрэглэнэ:</br>

        а/ зам тээврийн ослын улмаас зогссон үед;</br>

        б/ түр зогсох хориотой газарт зайлшгүй зогсолт хийсэн тээврийн хэрэгсэл нь ойртон ирж яваа бусад жолооч нарт харагдахааргүй тохиолдолд.</br>

9.5. Ослын зогсолтын тэмдгийг аюул учруулж болзошгүй талаас ойртон ирэх жолооч нарт тухайн саадыг урьдчилан анхааруулах зорилгоор тэдний ирэх зүгт хандуулан, тээврийн хэрэгслээс суурин газарт 15 м-ээс, суурин газрын гаднах замд 30 м-ээс, тууш замд 50 м-ээс багагүй зайд байрлуулна.</br>
9.6. Чирүүлж яваа механикжсан тээврийн хэрэгслийн ослын дохионы гэрэл нь ажиллахгүй (эсхүл байхгүй) бол ослын зогсолтын тэмдгийг уг тээврийн хэрэгслийн ард байрлуулж хөдөлгөөнд оролцоно.</br>

III. Чиг заах ба тоормослох дохио</br>

9.7. Жолооч хөдөлгөөн эхлэх, байр эзлэх, эргэх, зогсохын өмнө тээврийн хэрэгслийн эргэх чиг заадаг гэрлээр, хэрэв тэр нь ажиллахгүй буюу байхгүй бол гараар тухайн чигт хандсан дохио өгнө.</br>

9.8. Гараар өгөх дохиог бусад хөдөлгөөнд оролцогчдод сайтар харагдахуйц байдлаар гүйцэтгэнэ. Зүүн гар тийш дохио өгөхдөө зүүн гараа хажуу тийш чигээр нь сунгах буюу баруун гараа тохойгоор нь нугалж өргөнө. Баруун гар тийш дохио өгөхдөө баруун гараа хажуу тийш чигээр нь сунгах буюу зүүн гараа тохойгоор нь нугалж өргөнө.</br>

9.9. Чиг заах дохиог тодорхой үйлдэл хийхийн өмнө өгөх бөгөөд үйлдлээ хийж дуусмагц зогсооно. Харин уг дохиог гараар өгсөн бол үйлдлээ эхлэхийн өмнө дуусгаж болно.</br>

9.10. Тоормосны гэрэл нь уг тээврийн хэрэгсэл хурдаа хасаж байгааг анхааруулах бөгөөд хэрэв тэр нь ажиллахгүй буюу байхгүй бол жолооч тоормослохын өмнө бусад хөдөлгөөнд оролцогчдод сайтар харагдахуйц байдлаар гараа хажуу тийш сунган дээш доош хөдөлгөнө.</br>

9.11. Чиг заасан дохио нь бусад хөдөлгөөнд оролцогчдыг төөрөгдүүлэхгүй, тэдэнд буруу ойлголт төрүүлэхээргүй байвал зохино.</br>

IY. Дуут дохио</br>

9.12. Дуут дохиог зөвхөн дараахь тохиолдолд хэрэглэж болно:</br>

        а/ зам тээврийн ослоос урьдчилан сэргийлэх зорилгоор;</br>

        б/ суурин газрын гаднах замд гүйцэж түрүүлж байгаагаа бусад жолоочид анхааруулах зорилгоор;</br>

        в /төмөр замын гарам дээр зайлшгүй зогсолт хийсэн тохиолдолд энэ дүрмийн 17.7-гийн “б” заалтын дагуу.</br>

Y. Гэрэл шилжүүлэх дохио</br>

9.13. Гэрэл шилжүүлэх дохиог өгөхдөө харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд тээврийн хэрэгслийн холын ба ойрын гэрлийг ээлжлэн шилжүүлэх, өдрийн цагт асааж, унтраах байдлаар үйлдэнэ.
</br>
9.14. Гэрэл шилжүүлэх дохиог дараахь тохиолдолд хэрэглэж болно:</br>

        а/ гүйцэж түрүүлэх гэж байгаагаа бусад жолоочид анхааруулах зорилгоор;</br>

        б/ өөдөөс яваа тээврийн хэрэгслийн жолоочид холын гэрлээ ойрын гэрэлд шилжүүлэхийг анхааруулах зорилгоор;</br>

        в/ зам тавьж өгөхөөр зогсож байгаагаа тухайн хөдөлгөөнд оролцогчид мэдэгдэх зорилгоор.</br>

9.15. Гэрэл шилжүүлэх дохио нь бусад жолоочийн нүдийг гялбуулахаар бол хэрэглэхийг хориглоно.</br>

YI. Ойрын гэрэл</br>

9.16. Өдрийн цагт дараах тохиолдолд тээврийн хэрэгслийн ойрын гэрлийг асааж хөдөлгөөнд оролцоно:</br>

        а/ мотоцикл, мопед жолоодож яваа үед;</br>

        б/ хүүхдийг зохион байгуулалттай тээвэрлэж яваа үед;</br>

        в/ хүнд буюу овор ихтэй, мөн аюултай ачаа тээвэрлэж яваа үед;</br>

        г/ тусгай гэрлэн дохио ажиллуулсан тээврийн хэрэгслээр хамгаалуулж яваа үед.</br>

YII. Тээврийн хэрэгслийн таних тэмдэг</br>

9.17. Энэ дүрмийн 3 дугаар хавсралтад заасан хэмжээ, өнгө, загварын дагуу үйлдсэн таних тэмдгийг уг хавсралтад дурдсан тээврийн хэрэгсэлд байрлуулж тухайн жолооч, зорчигч, тээврийн хэрэгсэл, тээж яваа ачааны онцлогийг хөдөлгөөнд оролцогчдод мэдэгдэж анхааруулах зорилгоор хэрэглэнэ.</br>
