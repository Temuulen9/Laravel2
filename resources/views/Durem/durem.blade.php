    <h3> Гурав. Жолоочийн үүрэг </h3>

3.1. Механикжсан тээврийн хэрэгслийн жолооч дараахь бичиг баримттай байна:</br>

        а/ тухайн ангиллын тээврийн хэрэгслийг жолоодох эрхийн хүчинтэй үнэмлэх буюу түр зөвшөөрөл (жолоодлогын дадлага хийж яваа суралцагчид хамаарахгүй);</br>

        б/ тухайн тээврийн хэрэгслийн бүртгэлийн гэрчилгээ;</br>

        в/ жолоочийн даатгалын баталгаа;</br>

        г/ холбогдох хууль, эрх зүйн актад заасан ачааны болон бусад баримт бичиг;</br>

        д/ «Тулгуур эрхтний бэрхшээлтэй хүн» таних тэмдэг бүхий тээврийн хэрэгсэл жолоодоход тулгуур эрхтний бэрхшээлтэйг нотлох бичиг баримт.</br>

3.2. Монгол Улсын нутаг дэвсгэрт олон улсын хөдөлгөөнд оролцох механикжсан тээврийн хэрэгсэл нь тухайн бүртгэсэн улсынхаа ялгах тэмдэг, дугаартай байхаас гадна жолооч нь дараахь бичиг баримттай байна:</br>

        а/ замын хөдөлгөөний тухай Венийн конвенцид нийцсэн жолоодох эрхийн хүчинтэй үнэмлэх, тээврийн хэрэгслийн бүртгэлийн гэрчилгээ;</br>

        б/ Монгол Улсын хилээр тухайн тээврийн хэрэгслийг түр хугацаагаар нэвтрүүлсэн тухай гаалийн байгууллагын тэмдэглэгээ бүхий баримт;</br>

        в/ Монгол Улсын олон улсын гэрээ, хэлэлцээрт заасан зөвшөөрөл болон бусад шаардлагатай баримт бичиг.</br>

        Тайлбар: Тээврийн хэрэгслийн ялгах тэмдэг нь бүртгэлийн дугаарын тэмдэг дотор байж болно.</br>

3.3. Зохицуулагч, цагдаагийн алба хаагч, олон нийтийн цагдаа, автотээврийн болон байгаль орчны хяналтын улсын байцаагч энэ дүрмийн       8.16-д заасны дагуу зогсох дохио өгвөл жолооч тээврийн хэрэгслээ зогсоож, энэ дүрмийн 3.1, 3.2-т заасан бичиг баримт болон шаардлагатай тохиолдолд тээврийн хэрэгсэл, тээж яваа ачаа, бусад зүйлийг зохих журмын дагуу шалгуулна.</br>
        Тайлбар: Дээр дурдсан эрх бүхий ажилтан тухайн жолоочийн хүсэлтийн дагуу түүнд албаны үнэмлэхээ үзүүлэх үүрэгтэй.

3.4. Жолооч дараахь үүргийг хүлээнэ:</br>

        а/ энэ дүрмийн 4 дүгээр хавсралтад заасан эвдрэл, гэмтэл, техникийн зөрчилгүй байхаар тээврийн хэрэгслийнхээ бүрэн бүтэн байдлыг хангах;</br>

        Тээврийн хэрэгслийн ажлын тоормосны систем, жолооны механизм ажиллахгүй болсон, чиргүүлийн холбоос (чиргүүлтэй үед) эвдэрсэн, харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд хол, ойрын буюу ар талын оврын гэрэл асахгүй болсон, цас, бороо орж байгаа үед жолооч талын шил арчуур ажиллахгүй бол хөдөлгөөнөө үргэлжлүүлэхийг хориглоно.</br>

        Хөдөлгөөнд оролцож явах үед тээврийн хэрэгсэлд дээр дурдсанаас бусад гэмтэл (энэ дүрмийн 4 дүгээр хавсралтад заасан) тохиолдвол түүнийг засах, хэрэв бололцоогүй бол аюулгүй байдлыг хангаж аль ойр байгаа зогсоол (гараж) буюу засварын газарт очих хүртэл хөдөлгөөнөө үргэлжлүүлэхийг зөвшөөрнө;</br>

        б/ хамгаалах бүсээр тоноглосон автомашин жолоодохдоо хамгаалах бүс хэрэглэх, хамгаалах бүсийг хэрэглээгүй зорчигч тээвэрлэхгүй байх;</br>
 
       Тайлбар: Энэ заалт нь шуурхай албаны автомашины үүргээ гүйцэтгэж яваа жолооч, зорчигч, мөн биеийн эрүүл мэндийн байдалд үл тохирох заалт бүхий эмчийн магадлагаатай зорчигчид хамаарахгүй.</br>

        в/ мотоцикл, мопед жолоодохдоо хамгаалах малгайг зааврын дагуу өмсөх, хамгаалах малгай өмсөөгүй зорчигч тээвэрлэхгүй байх;</br>

        г/ хүний амь нас, эрүүл мэнд, эд хөрөнгөд ноцтой хохирол учирсан байж болзошгүй зам тээврийн ослын тохиолдол харсан даруйд очиж бололцоотой тусламж үзүүлэх, цагдаа, эмнэлгийн байгууллагад яаралтай мэдэгдэх;</br>

        д/ зам тээврийн осолд өртсөн хүнд эмнэлгийн анхны тусламж үзүүлэх, түүнийг эмнэлэгт яаралтай хүргэх;</br>

        е/ зам тээврийн осол, байгалийн гамшиг болон бусад аюул, эрсдэлд өртөж болзошгүй нь илэрхий байдалтай саатаж зогссон (зайлшгүй зогсолт хийсэн) жолоочийн дохиогоор зогсож, бололцоотой тусламж үзүүлэх;</br>

        ж/ согтууруулах ундаа, мансууруулах эм, сэтгэцэд нөлөөт бодис хэрэглэсэн эсэх буюу тээврийн хэрэгсэл жолоодоход эрүүл мэндийн хувьд тэнцэж байгаа эсэхийг, мөн шаардлагатай тохиолдолд тээврийн хэрэгсэл жолоодох ур чадвараа цагдаагийн эрх бүхий ажилтны шаардлагаар зохих журмын дагуу шалгуулах;</br>

        з/ зам дээр ажил, үүрэг гүйцэтгэж байгаа хүний дэргэдүүр өнгөрөхдөө хурдаа хасаж, түүнд аюул, осолтой байдал үүсгэхгүйгээр зорчих;</br>

        и/ цагдаа, онцгой байдал, эрүүл мэндийн байгууллагын ажилтнаас дайчлах тухай тавьсан хүсэлтийн дагуу гэмт хэрэг, үйлдвэрлэлийн осол, гал түймэр, байгалийн гамшиг, хүн, малын гоц халдварт өвчин зэрэг гэнэтийн аюул тохиолдсон буюу нийтийн эмх замбараагүй байдал үүссэн газарт хүрэлцэн очих, нүүлгэн шилжүүлэх, гэмт этгээдийг мөрдөн хөөх, амь нас, эрүүл мэндэд нь ноцтой хохирол учирч болзошгүй хүнийг эмнэлэгт, гэмт этгээдийг цагдаагийн байгууллагад хүргэхэд тээврийн хэрэгслээрээ үйлчлэх;</br>

        Тайлбар: Дээр дурдсан ажилд тээврийн хэрэгсэл дайчилсан ажилтан тухайн жолоочийн хүсэлтийн дагуу түүнд явсан зай, хугацаа, өөрийн овог нэр, албан тушаал, үнэмлэхний дугаараа тэмдэглэж албан ёсны баримт бичиж өгнө. Үүнийг үндэслэн тухайн жолоочид учирсан хохирол, зардлыг холбогдох хууль, журмын дагуу нөхөн төлнө.</br>

3.5. Зам тээврийн осолд холбогдсон жолооч дараахь үүргийг хүлээнэ:</br>

        а/ тээврийн хэрэгслээ нэн даруй зогсоож, ослын дохионы гэрлээ асаах ба ослын зогсолтын тэмдгийг энэ дүрмийн 9.5-д заасны дагуу тавьж, тээврийн хэрэгсэл болон осолд холбогдол бүхий эд юмсыг байрнаас нь хөдөлгөхгүй байх;</br>

        б/ осолд өртсөн хүнд эмнэлгийн анхны тусламж үзүүлж, түргэн тусламж дуудах, хугацаа алдаж болзошгүй тохиолдолд уг хүнийг ойр байгаа эмнэлэгт аль тааралдсан тээврийн хэрэгслээр хүргүүлэх буюу бололцоогүй бол өөрийн жолоодож яваа тээврийн хэрэгслээр хүргэж, эмнэлгийн ажилтанд биеийн байцаалт (иргэний үнэмлэх буюу жолоодох эрхийн үнэмлэх гэх мэт), тээврийн хэрэгслийн гэрчилгээг үзүүлж, өөрийн овог нэр, утасны дугаар, тээврийн хэрэгслийн марк, улсын дугаарыг тэмдэглүүлээд уг газартаа буцаж ирэх;</br>

        в/ осолд холбогдсон тээврийн хэрэгсэл нь бусад тээврийн хэрэгслийн хөдөлгөөнийг хаасны улмаас зорчих хэсгийг чөлөөлөх, мөн осолд өртсөн хүнийг эмнэлэгт уг тээврийн хэрэгслээр хүргэх зайлшгүй тохиолдолд хөндлөнгийн хоёроос доошгүй гэрчийг байлцуулан тухайн тээврийн хэрэгслийн ул мөрийн байрлалыг тодорхой тэмдэглэж, боломжтой бол гэрэл зураг, дүрс бичлэгээр баримтжуулсны дараа байрнаас нь хөдөлгөх;</br>

        г/ тухайн ослын талаар цагдаагийн байгууллага (ажилтан)-д яаралтай мэдэгдэж, ослыг гэрчлэх хүмүүсийн овог нэр, хаяг, утасны дугаарыг тэмдэглэж аваад цагдаагийн ажилтанг хүлээх ба түүнийг иртэл осолд холбогдол бүхий эд мөрийн баримт, ул мөрийг хамгаалах, бусад тээврийн хэрэгсэл тойрч гарах нөхцөлийг бүрдүүлэх талаар бололцоотой арга хэмжээ авах;</br>

       Тайлбар: Зам тээврийн ослыг харсан гэрч нь осолд өртсөн хүнд яаралтай тусламж үзүүлэх, цагдаагийн байгууллага (ажилтан)-д мэдэгдэх, эмнэлгийн түргэн тусламж дуудах, мөн боломжтой бол ослыг гэрчлэх бусад хүний овог нэр, хаяг, утасны дугаарыг тэмдэглэж авах, цагдаагийн ажилтанг иртэл хэргийн газраас явахгүй байх үүрэг хүлээнэ.</br>

        д/ согтууруулах ундаа, мансууруулах эм, сэтгэцэд нөлөөт бодис хэрэглэсэн эсэхийг шалгаж тогтоох зорилгоор шинжилгээ авах хүртэл, эсхүл ийм шинжилгээ авах шаардлагагүй гэж цагдаагийн ажилтан шийдвэр гаргах хүртэл энэ төрлийн зүйл хэрэглэхгүй байх.</br>

3.6. Зам тээврийн ослын үед хүний амь нас, эрүүл мэнд хохироогүй, осолд холбогдсон жолооч нарын тээврийн хэрэгслээс бусад эд хөрөнгөд хохирол учраагүй, ослоос үүдсэн эд хөрөнгийн хохирлыг жолооч нар өөр хоорондоо буюу даатгалын байгууллагатай харилцан маргаангүй шийдвэрлэхээр тохиролцсон бол энэ дүрмийн 3.5-ын “г”-д заасан үүргээс чөлөөлөгдөж болно.</br>

3.7. Жолоочид дараахь зүйлийг хориглоно:</br>

        а/ тухайн ангиллын тээврийн хэрэгслийг жолоодох эрхгүй (жолоодлогын дадлага хийхээс бусад тохиолдолд) , эсхүл согтууруулах ундаа, мансууруулах эм, сэтгэцэд нөлөөт бодис хэрэглэсэн, мөн хөдөлгөөний аюулгүй байдлыг хангаж чадахааргүй өвчтэй буюу ядарсан үедээ тээврийн хэрэгсэл жолоодох;</br>

        б/ тухайн ангиллын тээврийн хэрэгслийг жолоодох эрхгүй (жолоодлогын дадлага хийхээс бусад тохиолдолд), эсхүл согтууруулах ундаа, мансууруулах эм, сэтгэцэд нөлөөт бодис хэрэглэсэн, мөн хөдөлгөөний аюулгүй байдлыг хангаж чадахааргүй өвчтэй буюу ядарсан хүнд жолоогоо шилжүүлэх;</br>

        в/ зохих журмын дагуу улсын бүртгэлд хамруулаагүй буюу бүртгэлийн дугаарын тэмдэггүй, тэмдгийг буруу байрлуулсан, дугаар нь бүдгэрсэн, арилсан, түүнчлэн тогтоосон хугацаанд техникийн хяналтын үзлэгт оруулаагүй буюу тэнцээгүй тээврийн хэрэгслээр замын хөдөлгөөнд оролцох;</br>

       г/ эрх бүхий байгууллагаас тогтоосон жолоочийн хөдөлмөр, амралтын горимыг зөрчиж тээврийн хэрэгсэл жолоодох;</br>

       д/ тээврийн хэрэгслийн хөдөлгөөний үед гар утас болон түүнтэй ижил төстэй төхөөрөмж, хэрэгслийг гарны оролцоотойгоор ашиглах;</br>

       е/ зохицуулагчийн шүглийн чимээ, бусдын дуут дохиог сонсох боломжийг хязгаарласан чихэвч зүүх, хэт чанга хөгжим дуугаргах;</br>

        ж/ цуваанд яваа тээврийн хэрэгслүүдийн буюу жагсаалаар яваа хүмүүсийн дундуур нэвтрэн гарах, тэдний хооронд байр эзлэх;</br>

        з/ тээврийн хэрэгслээр зорчих хэсгийн хашлага давах (тэмдгээр тусгайлан зөвшөөрснөөс бусад тохиолдолд), тусгаарлах зурвас, аюулгүйн арал, таримал зүлгээр явах, замын хаалт, хашилт, тууз, чиглүүлэх хэрэгслээр хашиж хамгаалсан буюу хориглолт тогтоосон зам, талбай, ажлын талбар руу дур мэдэн нэвтрэх, батлагдсан замаас гарч шинэ зам гаргах, байгаль орчин, газрын хөрс, хадлан, тариалангийн талбай, ой модыг гэмтээх, гол мөрөн, нуур, булаг, хиймэл усан байгууламжийг бохирдуулах.</br>
