        <h3> Хорин долоо. Замын хөдөлгөөнд оролцогчоос бусад иргэн, хуулийн этгээдийн үүрэг </h3>

27.1. Монгол Улсын нутаг дэвсгэрт замын хөдөлгөөнд оролцох механикжсан тээврийн хэрэгсэл болон чиргүүл нь холбогдох эрх бүхий байгууллагаас олгосон улсын бүртгэлийн дугаарын тэмдэгтэй байх бөгөөд дугаарын тэмдгийг MNS 4410 стандартын дагуу байрлуулсан байна.</br>

27.2. Замын хөдөлгөөнд оролцогчоос бусад иргэн, хуулийн этгээд дараахь үүргийг хүлээнэ:</br>

        а/ 10 хүртэлх насны хүүхдийг харгалзах хүнгүйгээр замын хөдөлгөөнд явганаар оролцуулахгүй байх;</br>

        б/ өөрийн үйлдлийг хянах чадваргүй согтуурсан, мансуурсан байдалтай буюу сэтгэцийн өвчтэй хүнийг харгалзах хүнгүйгээр замын хөдөлгөөнд явганаар оролцуулахгүй байх;</br>

        в/ тээврийн хэрэгслийн техникийн байдалд тавих стандартын шаардлагыг хангахгүй буюу энэ дүрмийн 4 дүгээр хавсралтад заасан эвдрэл, гэмтэл, техникийн зөрчилтэй, эсхүл зохих журмын дагуу улсын бүртгэлд хамруулаагүй, бүртгэлийн дугаарын тэмдэггүй, тогтоосон хугацаанд техникийн хяналтын үзлэгт оруулаагүй буюу тэнцээгүй, автотээврийн болон өөрөө явагч хэрэгслийн татвар, агаарын бохирдлын төлбөрөө төлөөгүй, мөн энэ дүрмийн 3 дугаар хавсралтад заасан шаардлагын дагуу зохих таних тэмдгийг байрлуулаагүй тээврийн хэрэгслийг замын хөдөлгөөнд оролцуулахгүй байх;</br>

        г/ тухайн ангиллын тээврийн хэрэгслийг жолоодох эрхгүй (жолоодлогын дадлага хийхээс бусад тохиолдолд), эсхүл согтууруулах ундаа, мансууруулах эм, сэтгэцэд нөлөөт бодис хэрэглэсэн, мөн хөдөлгөөний аюулгүй байдлыг хангаж чадахааргүй өвчтэй буюу ядарсан хүнээр тээврийн хэрэгсэл жолоодуулахгүй байх;</br>

        д/ эрх бүхий байгууллагаас тогтоосон жолоочийн хөдөлмөр, амралтын горимыг зөрчсөн хүнээр тээврийн хэрэгсэл жолоодуулахгүй байх;</br>

        е/ тээврийн хэрэгслийн өмчлөгч нь холбогдох хууль, журмын дагуу жолоочийн даатгалд даатгуулсан байх;</br>

        ж/ гинж, ажлынхаа хэрэгслээр зам, замын байгууламжийг эвдэж болзошгүй трактор, өөрөө явдаг машин механизмыг хатуу хучилттай замаар явуулахгүй байх;</br>

        з/ хатуу хучилттай замд 50 м-ээс дотогш хариулгагүй мал, тэжээвэр амьтдыг оруулахгүй байх.</br>

27.3. Зам ашиглалт, хот тохижилтын асуудал эрхэлсэн байгууллагын ажилтан, албан тушаалтан нь зам, замын байгууламж, төмөр замын гарам болон хөдөлгөөн зохицуулах техник хэрэгслийг холбогдох норм, дүрэм, стандартын шаардлагын дагуу бүрэн бүтэн байлгах, хэрэв эдгээр нь эвдэрсэн буюу замын гадаргуу мөстсөн зэргээс хөдөлгөөнд аюул учирч болзошгүй үед түүнийг нэн даруй засах, цэвэрлүүлэх арга хэмжээ авна.</br>

27.4. Зам дээр засварын ажил, үйлчилгээ эрхэлж буй иргэн, хуулийн этгээд нь дараахь үүргийг хүлээнэ:</br>

        а/ засварын ажил, үйлчилгээ хийж байгаа үедээ хөдөлгөөний аюулгүй байдлыг хангах, 1.21 тэмдэг тавьж анхааруулах, замын тухайн хэсгийг хашиж тусгаарлах, түүнийг тойрч гарах чиг заасан тэмдэг байрлуулах;</br>

        б/ харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд жолоочид харагдах нөхцөлөө сайжруулах үүднээс гэрэл ойлгох шинж чанар бүхий хувцас хэрэглэл өмсөх;</br>

        в/ барилгын материал, тоног төхөөрөмж, ажиллаагүй машин механизмыг зорчих хэсгээс зайлуулах, хэрэв боломжгүй бол хаших, чиглүүлэх хэрэгслээр тусгаарлаж, тойрч гарах чиг заасан тэмдэг, харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд нэмэлт улаан буюу шар гэрэл тавьж анхааруулах;</br>

        г/ засварын ажил, үйлчилгээг дуусгасны дараа тээврийн хэрэгсэл, явган зорчигчийн аюулгүй, хэвийн зорчих нөхцөлийг бүрдүүлэх.</br>

27.5. Барилга, газар шорооны ажил эрхэлж буй иргэн, хуулийн этгээд нь хатуу хучилттай зам, төмөр замын гармаар зорчих тээврийн хэрэгслийн дугуйн шавар шавхайг цэвэрлүүлэх, замын гадаргууг бохирдуулахгүй байх арга хэмжээ авна.</br>  
