<h3> Тав. Явган зорчигчийн үүрэг </h3>

5.1. Явган зорчигч нь явган хүний замаар, тийм зам байхгүй бол зорчих хэсгийн хажуугийн хөвөөгөөр явна. Явган хүний зам, хөвөө байхгүй буюу түүгээр явах боломжгүй тохиолдолд явган зорчигч нь унадаг дугуйн зам, эсхүл зорчих хэсгийн захаар (тусгаарлах зурвастай замд зорчих хэсгийн гадна талын захаар) цуварч явна.</br>

Тэрэг түрсэн, чарга чирсэн, овор ихтэй ачаа барьсан буюу жагсаалаар яваа, мөн хөгжлийн бэрхшээлтэй хүний тэргэнцэртэй хүмүүс явган хүний зам, хөвөөгөөр явахад бусад явган зорчигчдод саад болохоор байвал зорчих хэсгийн захаар явж болно.</br>

Явган зорчигч зорчих хэсгийн захаар явахдаа тээврийн хэрэгслийн хөдөлгөөний чигийн эсрэг, харин хөгжлийн бэрхшээлтэй хүний тэргэнцэртэй буюу мотоцикл, мопед, унадаг дугуй хөтөлж яваа хүн хөдөлгөөний чигийн дагуу явна.</br>

Тайлбар: Нэг чигийн хөдөлгөөнтэй замын зорчих хэсгийн захаар явах тохиолдолд дээрх заалтыг мөрдөхгүй байж болно.</br>

5.2. Харанхуй үед гэрэлтүүлэггүй замд болон үзэгдэлт хангалтгүй нөхцөлд зорчих хэсгийн зах буюу хөвөөгөөр явах тохиолдолд явган зорчигч нь жолоочид харагдах нөхцөлөө сайжруулах үүднээс аль болох тод гэгээлэг өнгийн, боломжтой бол гэрэл ойлгох шинж чанар бүхий хувцас хэрэглэл, эд зүйлстэй явбал зохино. Мотоцикл, мопед, унадаг дугуй түрж яваа хүн тээврийн хэрэгслийнхээ гэрэлтүүлэх хэрэгсэл, гэрэл ойлгуурыг халхлахгүй явна.</br>

5.3. Жагсаалаар яваа хүмүүс зорчих хэсгээр явах тохиолдолд дөрвөөс илүүгүйгээр зэрэгцэж хөдөлгөөний чигийн дагуу баруун гар талаар явна. Жагсаалын зүүн гар талын захын урд болон ард, өдрийн цагт улаан дарцаг, харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд урд нь цагаан, ард нь улаан гэрэл (эсхүл гэрэл ойлгуур) барьсан хүн харгалзаж явна.</br>

5.4. Хүүхдийн жагсаалыг зөвхөн явган хүний замаар, явган хүний зам байхгүй бол зорчих хэсгийн хажуугийн хөвөөгөөр хоёроос илүүгүйгээр зэрэгцүүлж зөвхөн өдрийн цагаар явуулахыг зөвшөөрөх бөгөөд урд болон ард нь (хүүхдийн тоо гучаас олон бол жагсаалын мөн дунд хэсэгт) насанд хүрсэн хүн хамгаалж явна.</br>

5.5. Явган зорчигч нь 10 хүртэлх насны хүүхдийг замын хөдөлгөөнд харгалзаж явахдаа (энэ дүрмийн 5.4-т заасны дагуу жагсаалаар авч яваагаас бусад тохиолдолд) өөрөөсөө салгахгүй (хөтлөх, тэврэх, үүрэх, хүүхдийн тэргэнд түрэх гэх мэт) авч явна.</br>

5.6. Явган зорчигч нь явган хүний гарц буюу гармаар зам хөндлөн гарна. Хэрэв үзэгдэх хүрээнд явган хүний гарц, гарам байхгүй бол замын дагуу хоёр тийш сайн харагдах, хайс, хашилтгүй хэсгээр, ойртон ирж яваа тээврийн хэрэгслийг өнгөрүүлэн эгц хөндлөн гарна.</br>

5.7. Хөдөлгөөний зохицуулгатай хэсгээр явган зорчигч зам хөндлөн гарахдаа явган хүнд зориулсан гэрлэн дохио буюу зохицуулагчийн зөвшөөрсөн дохиогоор зорчих хэсэг рүү орох бөгөөд зорчих хэсгийг хөндлөн гарч амжаагүй байхад дохио өөрчлөгдвөл энэ дүрмийн 8.21-д заасныг баримтална. Явган зорчигчид зориулсан гэрлэн дохио байхгүй (эсхүл ажиллахгүй) бол явах чигийн өөдөөс хандан байрласан, зэрэгцээ хөдөлгөөний тээврийн хэрэгсэлд зориулсан гэрлэн дохиог удирдлага болгоно.</br>
5.8. Замын дагуу үзэгдэлт халхалсан саад, зогсож байгаа тээврийн хэрэгслийн дэргэдүүр зам хөндлөн гарахдаа тээврийн хэрэгсэл ойртон ирж байгаа эсэхийг магадлан мэдсэн байвал зохино.</br>

5.9. Явган зорчигч замын аль ч хэсэгт тусгай дуут болон гэрлэн дохио ажиллуулсан (шар өнгийн дохио ажиллуулж яваагаас бусад) автомашин, мотоцикл болон тэдгээрээр хамгаалуулж яваа тээврийн хэрэгслийг урьдчилан өнгөрүүлнэ. Ийм тээврийн хэрэгсэл ойртон ирж яваа үед зорчих хэсэг дээр явж байсан явган зорчигч аль болох яаралтай зорчих хэсгийг чөлөөлнө.</br>

5.10. Харааны бэрхшээлтэй хүн зорчих хэсгээр хөндлөн гарахдаа цагаан таяг барьсан байвал зохих бөгөөд хэрэв зорчих баримжаа алдаж, явган хүний гарцын байршлыг тогтоож чадахгүй болсон буюу гэрлэн дохионы заалтыг мэдэх боломжгүй бол бусдын туслалцааг авах, ийм бололцоогүй тохиолдолд таягаар сайтар дохио өгч анхаарал болгоомжтой зам хөндлөн гарна.</br>

Тайлбар: Цагаан таяг нь харанхуй үед гэрэл ойлгодог буюу гэрэлтдэг гадаргуутай байвал зохино.</br>

5.11. Чиглэлийн тээврийн хэрэгсэл, таксигаар үйлчлүүлэх хүмүүс болон замын дагуух тээврийн хэрэгслээр зорчих хүмүүс зориулалтын хүлээлгийн талбай дээр, ийм талбай байхгүй бол зорчих хэсгийн хашлаганы гадна талд (явган хүний зам дээр буюу хөвөөний гадна захад), хайс, хашилтгүй хэсэгт хүлээх бөгөөд тээврийн хэрэгсэл ирж бүрэн гүйцэд зогссон үед зорчих хэсэг рүү орохыг зөвшөөрнө. Тээврийн хэрэгслээс буусны дараа зорчих хэсгийг нэн даруй чөлөөлнө.</br>

5.12. Явган зорчигчид дараахь зүйлийг хориглоно:</br>

       а/ үзэгдэх хүрээнд явган хүний гарцтай замын гарцгүй хэсгээр, эсхүл явган хүний гарамтай замын гарамгүй хэсгээр зам хөндлөн гарах;</br>

        б/ аюулгүй байдлыг хангахаас бусад шалтгаанаар зорчих хэсэг дээр зогсох, саатах, зам хөндлөн гарч байхдаа алгуурлах, эргэж буцах, тээврийн хэрэгслийн хөдөлгөөнд санаатайгаар саад хийх;</br>

        в/ зорчих хэсгээр явж байхдаа утсаар ярих, гар утас болон түүнтэй ижил төстэй тоглоом, хэрэгсэл оролдож ширтэх, зохицуулагчийн шүглийн чимээ, бусдын дуут дохио, анхааруулгыг сонсох боломжийг хязгаарласан чихэвч зүүх, ном, сонин зэрэг зүйл унших;</br>

        г/ зорчих хэсэг дээр дугуйт тэшүүр, дугуйт хавтан болон түүнтэй ижил төстэй, биеийн тамир, спорт, тоглоомын зориулалттай хэрэгслээр явах;</br>

        д/ ойртон ирж яваа тээврийн хэрэгслийн урдуур гэнэт гүйх.</br>
