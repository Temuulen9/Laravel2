<h3> Найм. Замын хөдөлгөөн зохицуулах дохио </h3>

8.1. Замын хөдөлгөөнийг гэрэл буюу зохицуулагчийн дохиогоор зохицуулна.</br>

8.2. Замын хөдөлгөөнд оролцогч нь өөрийн явж байгаа чигийн өөдөөс хандан байрласан замын гэрлэн дохио буюу замын тухайн хэсэгт хөдөлгөөн зохицуулж байгаа зохицуулагчийн дохиог дагаж мөрдөнө.</br>

8.3. Зохицуулагчийн дохиогоор хөдөлгөөн зохицуулж байгаа газарт гэрэл дохио болон дарааллын тэмдгийн заалтыг, гэрлэн дохиогоор зохицуулж байгаа бол дарааллын тэмдгийн заалтыг мөрдөхгүй.</br>

I. Замын гэрлэн дохио</br>

8.4. Замын хөдөлгөөнийг зохицуулахад MNS 4980 стандартаар тогтоосон улаан, шар, ногоон өнгийн гэрэл бүхий босоо буюу хэвтээ байрлалтай гэрлэн дохиог MNS 4596 стандартад заасны дагуу хэрэглэнэ.</br>

8.5. Тээврийн хэрэгслийн хөдөлгөөн зохицуулах гэрлэн дохио (энэ дүрмийн 8.7-д зааснаас бусад) нь улаан, шар, ногоон өнгийн дарааллаар дээрээс доош, эсхүл ийм дарааллаар зүүнээс баруун гар тийш байрласан гэрлүүдтэй байх бөгөөд ногоон гэрэл нь чиг заасан сумтай байж болно.</br>

Мөн хар өнгийн дэвсгэр дээр ногоон өнгөөр гэрэлтдэг сум бүхий нэг буюу хоёр нэмэгдэл дохиотой байж болох ба энэ тохиолдолд үндсэн ногоон гэрэл нь нэмэгдэл гэрэл дохионы сумнаас бусад чигийг заасан хар өнгийн сумтай байна. Зүүн гар тийш чиг заасан нэмэгдэл гэрэл дохио нь үндсэн ногоон гэрлийн зүүн гар талд, харин баруун гар тийш чиг заасан нэмэгдэл гэрэл дохио нь босоо байрлалтай гэрлэн дохионы хувьд үндсэн ногоон гэрлийн баруун гар талд, хэвтээ байрлалтай гэрлэн дохионы хувьд доор нь байрласан байна.</br>

8.6. Явган зорчигчийн хөдөлгөөн зохицуулах гэрлэн дохио нь босоо байрлалтай байх бөгөөд дээд талдаа улаан өнгөөр гэрэлтдэг зогсож буй хүний дүрс, доод талдаа ногоон өнгөөр гэрэлтдэг алхаж буй хүний дүрс бүхий дохиотой байна. Мөн харааны бэрхшээлтэй хүмүүст явган хүний гарцын байршлыг мэдэгдэх буюу тухайн гэрлэн дохионы ажиллагааны горимыг мэдээлэхэд зориулсан нэмэгдэл дуут дохиотой байж болно.</br>

8.7. Төмөр замын гармаар нэвтрэх хөдөлгөөнийг зохицуулах гэрлэн дохио нь анивчдаг буюу ээлжлэн анивчдаг улаан өнгийн нэг буюу хоёр гэрэл бүхий дохиотой байх бөгөөд нэмэгдэл дуут дохио (хонх)-той байж болно.</br>

8.8. Унадаг дугуйн хөдөлгөөн зохицуулах гэрлэн дохио нь 7.4.ж нэмэлт тэмдэгтэй байна.</br>

8.9. Гэрэл дохио нь дараахь утгатай байна:</br>

        а/ ногоон гэрэл дохио хөдөлгөөнийг зөвшөөрнө;</br>

        Тайлбар: Ногоон гэрэл дохио унтрах хүртэл үлдэж байгаа хугацааг жолооч болон явган зорчигчид мэдээлэх зориулалттай тоон буюу хуваарьт хэмжүүр бүхий дэлгэцийг гэрлэн дохиотой хамт байрлуулсан байж болно.</br>

        б/ шар гэрэл дохио нь гэрэл дохио солигдох гэж байгааг анхааруулах бөгөөд энэ дүрмийн 8.19-т зааснаас бусад тохиолдолд замын тухайн хэсэг рүү (энэ дүрмийн 8.18-д заасан зогсох газар руу) нэвтрэн орохыг хориглоно;</br>

        в/ анивчсан шар гэрэл дохио нь хөдөлгөөнийг зөвшөөрөх бөгөөд тухайн уулзвар буюу явган хүний гарц зохицуулгагүй болсныг мэдээлж болгоомжтой явахыг анхааруулна;</br>

        г/ улаан гэрэл дохио хөдөлгөөнийг хориглоно.</br>

8.10. Нэмэгдэл гэрэл дохио нь зөвхөн сум заасан чигийн хөдөлгөөнийг зохицуулах бөгөөд үндсэн гэрэл дохио нь нэмэгдэл дохионы зааснаас бусад чигийн хөдөлгөөнийг зохицуулна.</br>

8.11. Чиг заасан сум бүхий үндсэн ногоон гэрэл болон нэмэгдэл гэрэл дохио нь зөвхөн сум заасан чигт хөдөлгөөнийг зөвшөөрнө. Зүүн гар тийш эргэхийг зөвшөөрсөн сум нь буцаж эргэхийг мөн зөвшөөрнө. Нэмэгдэл гэрэл дохио асаагүй бол (гэрлэн дохионы зохицуулгатай үед) уг чигт хөдөлгөөнийг хориглоно.</br>

8.12. Баруун гар тийш чиг заасан нэмэгдэл гэрэл дохиог байнгын асаалттай горимд ажиллуулж тухайн талаас уулзвараар баруун гар тийш эргэх тээврийн хэрэгслийн хөдөлгөөнийг нээлттэйгээр зохион байгуулж болно. Гэрэл дохионы ийм зохицуулгатай уулзвараар жолооч баруун гар тийш эргэхдээ энэ дүрмийн          15.5-д заасныг баримтална.</br>

II. Зохицуулагчийн дохио</br>

8.13. Зохицуулагч биеийн байрлал, гар, дохиурын заалтаар дохио өгөхөөс гадна хөдөлгөөнд оролцогчдын анхаарлыг өндөржүүлэх үүднээс шүглээр дохио өгнө.</br>

8.14. Зохицуулагчийн хөдөлгөөн зохицуулах дохио нь дараахь утгатай байна:</br>
        а/ баруун гараа дээш өргөсөн байвал энэ дүрмийн 8.19-т зааснаас бусад тохиолдолд тээврийн хэрэгсэл замын тухайн хэсэг рүү (энэ дүрмийн        8.18-д заасан зогсох газар руу) явган зорчигч зорчих хэсэг рүү нэвтрэн орохыг хориглоно;</br>

        б/ баруун гараа цээжиндээ авсан буюу гараа алдалсан байвал зохицуулагчийн зүүн болон баруун гар талаас яваа тээврийн хэрэгсэл чигээрээ буюу баруун гар тийш нэвтрэх, явган зорчигч зам хөндлөн гарахыг зөвшөөрөх бөгөөд зохицуулагчийн ар, өвөр талаас яваа тээврийн хэрэгсэл, явган зорчигчийн хөдөлгөөнийг хориглоно;</br>

        в/ баруун гараа урагш сунгасан байвал зохицуулагчийн зүүн гар талаас яваа тээврийн хэрэгсэл бүх чигт, өвөр талаас яваа тээврийн хэрэгсэл зөвхөн баруун гар тийш эргэхийг зөвшөөрөх бөгөөд бусад талаас (баруун гар буюу ар талаас) яваа тээврийн хэрэгслийн хөдөлгөөнийг хориглоно. Явган зорчигч зохицуулагчийн зөвхөн ар талаар зам хөндлөн гарахыг зөвшөөрнө.</br>

8.15. Зохицуулагч нь жолооч, явган зорчигчдод хөдөлгөөний чиг өөрчлөх, байр эзлэх, хурд хасах буюу нэмэх, тойрон гарах, зогсох зэрэг үйлдэл хийхийг заасан ойлгогдохуйц дохио өгч болно.</br>

8.16. Зохицуулагч дохиураа (эсхүл гараараа) тээврийн хэрэгсэл рүү чиглүүлэн зааж дохих, эсхүл чанга яригч хэрэгслээр шаардвал уг тээврийн хэрэгслийн жолооч зохицуулагчийн заасан газарт буюу замын тухайн хэсэгт бусдын хөдөлгөөнд саад болохооргүй тохиромжтой байрлал сонгож зогсоно.</br>

8.17. Зохицуулагчийн ойлгогдохуйц дохио нь замын гэрэл дохио, тэмдэг, тэмдэглэл болон энэ дүрмийн бусад заалттай зөрчилдөж байвал хөдөлгөөнд оролцогчид зохицуулагчийн дохиог удирдлага болгоно.</br>

III. Хориглосон дохиогоор зогсох буюу хөдөлгөөнөө үргэлжлүүлэх</br>

8.18. Гэрэл буюу зохицуулагчийн хориглосон дохиогоор жолооч «Зогс-шугам» (1.11 тэмдэглэл буюу 5.32 тэмдэг)-ийн өмнө зогсох бөгөөд хэрэв тийм тэмдэг, тэмдэглэл байхгүй бол тухайн уулзвар, явган хүний гарцын өмнө, бусад газарт тээврийн хэрэгсэл, явган зорчигчийн хөдөлгөөнд саад болохгүйгээр гэрлэн дохио, зохицуулагчийн наана, эсхүл энэ дүрмийн 17.5-д заасны дагуу төмөр замын гармын өмнө зогсоно.</br>

8.19. Энэ дүрмийн 8.18-д заасан газарт дөхөж ирсэн жолооч шар гэрэл дохио асахад буюу зохицуулагч гараа өргөх үед огцом тоормослохгүйгээр зогсох боломжгүй тохиолдолд аюулгүй байдлыг хангаж хөдөлгөөнөө үргэлжлүүлэхийг зөвшөөрнө.</br>

8.20. Энэ дүрмийн 8.18-д заасан газрыг зөвшөөрсөн дохиогоор нэвтрэн орсон жолооч дохио өөрчлөгдвөл аюулгүй байдлыг хангаж, явж байгаа чигтээ хөдөлгөөнөө үргэлжлүүлнэ. Харин уулзвар дээр жолоочийн явах чигийн өмнө «Зогс-шугам» (1.11 тэмдэглэл буюу 5.32 тэмдэг) бүхий гэрлэн дохио байвал түүнийг удирдлага болгоно.</br>

8.21. Зөвшөөрсөн дохиогоор явган хүний гарц руу орсон явган зорчигч дохио өөрчлөгдвөл аль болох яаралтай зорчих хэсгийг нэвтрэн гарах бөгөөд боломжгүй бол тээврийн хэрэгслийн хөдөлгөөний эсрэг урсгалын зааг дээр зогсож аюулгүй байдал хангагдсан үед хөдөлгөөнөө үргэлжлүүлэх буюу дараагийн зөвшөөрөх дохиог хүлээж нэвтэрнэ.</br>
