<h3> Хорин тав. Унадаг дугуй, мопедоор хөдөлгөөнд оролцоход тавих нэмэлт шаардлага </h3>

25.1. Унадаг дугуйн тоормос, жолоо, хонх нь бүрэн ажиллагаатай байхын дээр харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд хөдөлгөөнд оролцоход урд талдаа цагаан, ар талдаа улаан өнгийн гэрэл буюу гэрэл ойлгуур, хоёр хажуу талдаа улаан буюу улбар шар өнгийн гэрэл ойлгууртай байна.</br>

Мопед нь энэ дүрмийн 4 дүгээр хавсралтаар түүнд хамаатуулан заасан эвдрэл, гэмтэл, техникийн зөрчилгүй байна.</br>

25.2. Харанхуй үед гэрэлтүүлэггүй замд болон үзэгдэлт хангалтгүй нөхцөлд унадаг дугуй, мопедын жолооч нь бусад тээврийн хэрэгслийн жолоочид харагдах нөхцөлөө сайжруулах үүднээс аль болох тод гэгээлэг өнгийн, боломжтой бол гэрэл ойлгох шинж чанар бүхий хувцас хэрэглэл, эд зүйлстэй явбал зохино.</br>

25.3. Унадаг дугуйг 10 хүртэлх насны хүүхэд харгалзах хүнтэйгээр зөвхөн явган хүний зам дээр, харин 10-аас 14 хүртэлх насны хүүхэд явган хүний зам болон түүн дээр тэмдэглэгдсэн унадаг дугуйн замаар унаж хөдөлгөөнд оролцож болно. 16 нас хүрээгүй бол мопед жолоодож замын хөдөлгөөнд оролцохыг хориглоно.</br>

25.4. Унадаг дугуйг 14-өөс, мопедыг 16-аас дээш насны хүн жолоодож замын хөдөлгөөнд оролцохдоо унадаг дугуйн зам буюу унадаг дугуйн эгнээгээр явах бөгөөд дараахь тохиолдолд зорчих хэсгийн баруун гар талын захаар байрлан явж болно:</br>

        а/ унадаг дугуйн зам болон унадаг дугуйн эгнээ байхгүй, эсхүл түүгээр явах боломжгүй тохиолдолд;</br>

        б/ ачаатай буюу ачаагүй өргөн нь 1 м-ээс илүү бол;</br>

        в/ зохион байгуулалттай цуваагаар яваа үед.</br>

        Тайлбар: Зорчих хэсгийн захаар байрлан явахдаа зохион байгуулалттай цуваагаар яваа тохиолдолд нэг эгнээнд хоёроос илүүгүйгээр зэрэгцэн явж болох бөгөөд бусад тохиолдолд зөвхөн ар араасаа цуварч явна.</br>

25.5. Энэ дүрмийн 25.4-т заасны дагуу зорчих хэсгийн захаар байрлан явахад хүндрэлтэй буюу боломжгүй байвал унадаг дугуй, мопед нь хөвөөгөөр, эсхүл унадаг дугуй нь явган хүний замаар явж болох бөгөөд ингэхдээ аюулгүй байдлыг хангаж, хурдыг цагт 10 км-ээс хэтрүүлэхгүй, явган зорчигчдод саад учруулахгүй байвал зохино. Хэрэв явган зорчигчийн хөдөлгөөн ихтэй байвал бууж явгалан, явган зорчигчийн үүргээр хөдөлгөөнд оролцоно.</br>

25.6. Унадаг дугуй, мопедыг жолоодож явган хүний гарцаар зам хөндлөн гарах, мөн тухайн чигт хоёр буюу түүнээс олон эгнээгээр зорчдог замаас зүүн гар тийш буюу буцаж эргэхийг хориглоно. Энэ тохиолдолд тээврийн хэрэгслээсээ бууж хөтлөн явган зорчигчийн үүргээр зам хөндлөн гарна.</br>

25.7. Унадаг дугуй, мопедын жолооч нь зорчих хэсгээр байрлан явахдаа зөвхөн гүйцэх, гүйцэж түрүүлэх, саадыг тойрон гарахын тулд зүүн гар талын эгнээнд орохыг зөвшөөрнө. Хэрэв уг үйлдлийг эгнээнээс гарахгүйгээр гүйцэтгэх боломжтой бол зүүн гар талын эгнээ рүү орохыг хориглоно.</br>

25.8. Унадаг дугуй, мопедын жолооч нь уулзвар, гарцаас өөр хэсэгт унадаг дугуйн замаар зорчих хэсгийг огтлон гарахдаа тухайн замаар яваа тээврийн хэрэгсэлд зам тавьж өгнө.</br>

25.9. Унадаг дугуй, мопедын жолоочид дараахь зүйлийг хориглоно:</br>

        а/ зам дээр жолоодлогын (унаж сурах) дадлага хийх, сайтар дадлагажаагүй үедээ замын хөдөлгөөнд оролцох;</br>
        б/ залуурыг сул тавих, дөрөөнд гишгэхгүй явах;</br>

        в/ унадаг дугуй, мопедын овроос уртаашаа буюу өргөөшөө 0.4 м-ээс илүү гарсан ачаа тээвэрлэх;</br>

        г/ зорчигчид зориулсан суудалгүй тохиолдолд буюу ачааны зориулалттай тэвшинд хүн тээвэрлэх;</br>

        д/ 7 хүртэлх насны хүүхдийг зориулалтын нэмэгдэл суудал, хамгаалах хэрэгсэлгүйгээр тээвэрлэх;</br>

