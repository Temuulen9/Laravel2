<h3>Арван найм. Гадна талын гэрэлтүүлэх хэрэгслийг хэрэглэх</h3>

18.1. Харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд, мөн хонгил дотор жолооч тээврийн хэрэгслийн гадна талын гэрлийг дараахь байдлаар асааж явна:</br>

        а/ замын гэрэлтүүлэггүй хэсэгт холын буюу ойрын гэрлийг асаана;</br>

        б/ замын гэрэлтүүлэгтэй хэсэгт ойрын гэрлийг асаана;</br>

        в/ чиргүүлтэй яваа үед түүний оврын гэрлийг асаана.</br>

18.2. Дараахь тохиолдолд холын гэрлийг ойрын гэрэлд шилжүүлнэ:</br>

        а/ өөдөөс яваа тээврийн хэрэгсэл хүртэл 150 м-ээс багагүй зайд;</br>

        б/ өөдөөс яваа тээврийн хэрэгслийн жолооч гэрэл шилжүүлэх дохио өгвөл зайн хэмжээг үл харгалзан;</br>

        в/ бусад жолоочийн нүдийг гялбуулж болзошгүй тохиолдолд.</br>

18.3. Хэрэв жолоочийн нүд гялбан зорчих баримжаа алдвал ослын дохионы гэрлээ асааж, эзэлж явсан эгнээгээ өөрчлөхгүйгээр хурдаа хасах буюу зогсоно.</br>

18.4. Харанхуй үед замын гэрэлтүүлэггүй хэсэгт болон үзэгдэлт хангалтгүй нөхцөлд тээврийн хэрэгслийг зогсоохдоо оврын гэрлийг асаана. Хэрэв тэр нь асахгүй буюу байхгүй бол тээврийн хэрэгслийг замаас гаргаж зогсоох бөгөөд ийм бололцоогүй тохиолдолд энэ дүрмийн 9.3-ын “в”, 9.4, 9.5 заалтын дагуу анхааруулах дохио хэрэглэнэ.</br>

Тайлбар: Үзэгдэлт хангалтгүй нөхцөлд тээврийн хэрэгслийг зогсоохдоо оврын гэрэлтэй хамт ойрын буюу манангийн гэрлийг асааж болно.</br>

18.5. Харанхуй үед болон үзэгдэлт хангалтгүй нөхцөлд чиргүүл чирч явахдаа ба зогсож байхдаа бүхээг дээрх «Чиргүүлтэй тээврийн хэрэгсэл» таних тэмдгийг асаана.</br>

18.6. Манангийн гэрлийг үзэгдэлт хангалтгүй нөхцөлд болон мөн харанхуй үед замын гэрэлтүүлэггүй хэсэгт холын буюу ойрын гэрлийн хамт хэрэглэж болно.</br>

18.7. Гэрэлтүүлүүрийг суурин газрын гаднах замд өөдөөс тээврийн хэрэгсэлгүй үед хэрэглэхийг зөвшөөрнө. Түүнийг суурин газарт тусгай дуут болон гэрлэн дохиогоор тоноглосон автомашины жолооч албан үүрэг гүйцэтгэж явахдаа шаардлагатай үед хэрэглэж болох бөгөөд харин бусад тээврийн хэрэгсэлд суурилуулахыг хориглоно.</br>

18.8. Тээврийн хэрэгсэлд өдрийн цагаар асааж явахад зориулан үйлдвэрээс тоноглосон гэрлийг жолооч өдрийн цагт хөдөлгөөнд оролцохдоо асааж болно.</br>

