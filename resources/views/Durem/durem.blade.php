<h3>Арван тав. Уулзвар нэвтрэх</h3>

15.1. Жолооч уулзвараар баруун, зүүн гар тийш эргэхдээ тухайн хөндлөн замын зорчих хэсгийг явган хүний гарцаар хөндлөн гарч яваа явган зорчигч, мөн унадаг дугуйн зам буюу эгнээний огтлолцлоор хөндлөн гарч яваа унадаг дугуй, мопедод зам тавьж өгнө</br>

15.2. Уулзварыг бүрэн гүйцэд нэвтрэн гарах боломжийг хязгаарласан түгжрэл, саатал бий болсон үед жолооч (уулзвар нэвтрэх давуу эрхтэй буюу зөвшөөрсөн гэрэл дохио асаж байгаа эсэхээс үл хамааран) уулзвар руу орохыг хориглоно.</br>

15.3. Гэрэл буюу зохицуулагчийн дохиогоор хөдөлгөөний дараалал тогтоож байгаа уулзварыг зохицуулдаг уулзвар гэнэ. Гэрлэн дохио байхгүй буюу ажиллаагүй, эсхүл анивчсан шар гэрлийн горимд ажиллаж байгаа, мөн зохицуулагч хөдөлгөөн зохицуулаагүй уулзварыг зохицуулдаггүй уулзварт тооцно.</br>

I. Зохицуулдаг уулзвар нэвтрэх</br>

15.4. Жолооч ногоон гэрэл дохиогоор зүүн гар тийш буюу буцаж эргэхдээ уулзварт ороод, өөдөөс чигээрээ яваа болон баруун гар тийш эргэх тээврийн хэрэгсэлд зам тавьж өгнө.</br>

15.5. Улаан буюу шар гэрэлтэй хамт ассан нэмэгдэл гэрэл дохиогоор явж буй жолооч бусад чиглэлээс яваа хөдөлгөөнд оролцогчдод зам тавьж өгнө.</br>

15.6. Зохицуулагчийн дохиогоор жолооч буцаж эргэхдээ зүүн гар талын замаас баруун гар тийш эргэж байгаа тээврийн хэрэгсэлд зам тавьж өгнө.</br>

15.7. Зөвшөөрсөн гэрэл дохио асах үед жолооч нь уулзвар нэвтрэх үйлдлээ дуусгаж байгаа тээврийн хэрэгсэл, зорчих хэсгийг хөндлөн гарч амжаагүй яваа явган зорчигчид зам тавьж өгнө.</br>

II. Зохицуулдаггүй уулзвар нэвтрэх</br>

15.8. Адил замын уулзварт жолооч баруун гар талаас ирсэн тээврийн хэрэгсэлд зам тавьж өгнө.</br>

15.9. Гол ба туслах замын уулзварт туслах замаас яваа жолооч гол замаас ирсэн тээврийн хэрэгсэлд зам тавьж өгнө.</br>

15.10. Жолооч зүүн гар тийш буюу буцаж эргэхдээ уулзварт ороод, адил замаар өөдөөс чигээрээ яваа болон баруун гар тийш эргэх тээврийн хэрэгсэлд зам тавьж өгнө.</br>

15.11. Уулзвар дээр гол зам чигээ өөрчилсөн байвал тэр замаас яваа жолооч нар өөр хоорондоо адил замын уулзвар нэвтрэх журам баримтална. Туслах замаас яваа жолооч нар өөр хоорондоо мөн энэ заалтыг баримтална.</br>

15.12. Дарааллын тэмдэггүй уулзварыг нэвтрэхийн өмнө жолооч уг уулзварт нийлсэн зорчих хэсгүүдийн гадаргуугийн ялгааг тодорхойлох боломжгүйгээс (харанхуй, манантай, цас орсон, бохирдсон зэргээс) нэвтрэх дараалал тогтооход хүндрэлтэй байвал өөрийгөө туслах замаар яваад тооцно.</br>

