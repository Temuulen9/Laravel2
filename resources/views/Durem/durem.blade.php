22.3. Хатуу холбоогоор чирэх, чирүүлэх тээврийн хэрэгслүүдийн хоорондын зай 4 м-ээс илүүгүй байх бөгөөд чирүүлэх тээврийн хэрэгслийн жолооны механизм бүрэн ажиллагаатай байвал зохино.</br>

22.4. Зөөлөн холбоогоор чирэх, чирүүлэх тээврийн хэрэгслүүдийн хоорондын зай 4 м-ээс 6 м байх бөгөөд чирүүлэх тээврийн хэрэгслийн тоормосны систем, жолооны механизм, дуут дохио бүрэн ажиллагаатай байвал зохино. Зөөлөн холбооны нэг метр тутамд 200 мм х 200 мм-ээс багагүй хэмжээний улаан дарцгийг унжуулж бэхэлсэн байх бөгөөд харанхуй үед дарцаг нь гэрэл ойлгох шинж чанартай байвал зохино.</br>

22.5. Механикжсан тээврийн хэрэгслийг чирч яваа автобус, троллейбусанд болон ачааны автомашины тэвшинд, мөн чирүүлж яваа механикжсан тээврийн хэрэгсэлд хүн тээвэрлэхийг хориглоно.</br>

