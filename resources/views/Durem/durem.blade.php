<h3> Дөрөв. Тусгай дуут болон гэрлэн дохио ажиллуулсан
          тээврийн хэрэгслийн хөдөлгөөн </h3>

4.1. Тусгай дуут болон гэрлэн дохиогоор тоноглосон автомашин, мотоциклын жолооч нь тухайн ангиллын тээврийн хэрэгслийг гурваас дээш жил жолоодож дадлагажсан байна. Тусгай дуут болон гэрлэн дохионы өнгө, давтамж, хөг ая нь MNS 4979 “Тээврийн хэрэгсэлд ашиглах тусгай дуут болон гэрлэн дохио. Ерөнхий шаардлага” (цаашид стандартын зөвхөн дугаарыг зааж бичнэ) стандартад нийцсэн байна.</br>

4.2. Улаан, хөх, хөх-улаан, ногоон өнгийн тусгай гэрлэн дохио ажиллуулан хойшлуулшгүй албан үүрэг гүйцэтгэж яваа тээврийн хэрэгслийн жолооч хөдөлгөөний аюулгүй байдлыг хангасан нөхцөлд энэ дүрмийн 7, 8 дугаар бүлэг (зохицуулагчийн дохиотой холбоотой заалтаас бусад), 10-17, 19, 20 дугаар бүлэг болон 1 ба 2 дугаар хавсралтын заалтыг мөрдөхгүй байж болно. Тусгай дуут дохиогоо хамт ажиллуулсан тохиолдолд давуу эрхтэй байх бөгөөд давуу эрх эдлэхдээ бусад хөдөлгөөнд оролцогч өөрт нь зам тавьж өгч байгааг мэдсэн байвал зохино. Энэ эрх нь дээрх тээврийн хэрэгслээр хамгаалуулж яваа тээврийн хэрэгслийн жолоочид мөн адил хамаарна.</br>

4.3. Шар өнгийн тусгай гэрлэн дохио ажиллуулан зам, замын байгууламжийн засвар, арчлалтын ажил гүйцэтгэж яваа болон гэмтэл, зөрчилтэй тээврийн хэрэгслийг шилжүүлэн зөөж яваа тээврийн хэрэгслийн жолооч хөдөлгөөний аюулгүй байдлыг хангасан нөхцөлд энэ дүрмийн 9.3-ын “е”, 9.6, 11.2, 11.6-11.8, 11.10, 11.12, 20.1 заалт болон замын тэмдэг (2.11-2.14, 2.20, 3.2, 3.4-3.6 тэмдгүүдээс бусад), тэмдэглэлийн заалтыг мөрдөхгүй байж болно.</br>

Мөн шар өнгийн тусгай гэрлэн дохио ажиллуулан хүнд буюу овор ихтэй ачаа тээвэрлэж яваа (эсхүл ийм ачаатай тээврийн хэрэгслийг хамгаалж яваа) тээврийн хэрэгслийн жолооч хөдөлгөөний аюулгүй байдлыг хангасан нөхцөлд замын тэмдэглэлийн заалтыг мөрдөхгүй байж болно.</br>

Шар өнгийн тусгай гэрлэн дохио нь хөдөлгөөнд оролцогчдын анхаарлыг өндөржүүлэх зориулалттай бөгөөд давуу эрх олгохгүй.</br>

Тайлбар: Шар өнгийн тусгай гэрлэн дохиог тусгай дуут дохиотой хэрэглэхгүй.</br>

4.4. Тусгай дуут болон гэрлэн дохио ажиллуулсан (шар өнгийн дохио ажиллуулж яваагаас бусад) автомашин, мотоцикл болон тэдгээрээр хамгаалуулж яваа тээврийн хэрэгсэл ойртон ирж яваа үед бусад тээврийн хэрэгслийн жолооч зам тавьж өгөн, чөлөөтэй зорчих боломжийг хангана.</br>

4.5. Тусгай дуут болон гэрлэн дохио ажиллуулсан (шар өнгийн дохио ажиллуулж яваагаас бусад) автомашин, мотоцикл болон тэдгээрээр хамгаалуулж яваа тээврийн хэрэгслийг гүйцэх, гүйцэж түрүүлэхийг хориглоно.</br>

4.6. Улаан, хөх, хөх-улаан өнгийн тусгай гэрлэн дохио ажиллуулсан тээврийн хэрэгсэл ойртон ирж, тусгай дохиогоо дуугаргах буюу чанга яригч хэрэгслээр зогсохыг шаардвал жолооч энэ дүрмийн 14.1-д заасны дагуу зогсох бөгөөд хэрэв өөр албан шаардлага тавибал түүнийг нэн даруй биелүүлнэ.</br>

4.7. Тусгай гэрлэн дохио ажиллуулсан тээврийн хэрэгсэл зогсож байвал ойртон ирсэн жолооч шаардлагатай тохиолдолд нэн даруй зогсох боломжтой байхаар хурдаа хасна.</br>

4.8. Зохих журмын дагуу эрх бүхий байгууллагаас олгосон зөвшөөрөлгүйгээр тусгай дуут болон гэрлэн дохиог тээврийн хэрэгсэлд байрлуулахыг хориглоно.</br>
