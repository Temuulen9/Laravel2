<h3> Долоо. Замын тэмдэг ба тэмдэглэл </h3>

7.1. Замын хөдөлгөөнийг зохион байгуулахад MNS 4597, MNS 4759 стандартаар тогтоосон замын тэмдэг, тэмдэглэлийг MNS 4596 стандартад заасны дагуу хэрэглэнэ.</br>

Тайлбар: Дээрх стандартаар тогтоосон замын тэмдэг, тэмдэглэлийн дугаар, нэр, дүрс, утга агуулга, үйлчлэх нөхцөлийг энэ дүрмийн 1 ба 2 дугаар хавсралтад тусгана.</br>

7.2. Замын хөдөлгөөнд оролцогч нь өөрийн явж байгаа чигийн өөдөөс хандан байрласан замын тэмдгийг энэ дүрмийн 1 дүгээр хавсралт, тэмдэглэлийг     2 дугаар хавсралтад заасан утга агуулга, үйлчлэлийн хүрээнд дагаж мөрдөнө.</br>

7.3. Энэ дүрэмд тусгайлан зааснаас бусад тохиолдолд замын тухайн хэсэгт байрлуулсан тэмдэг, тэмдэглэлийн заалт дүрмийн бусад заалттай зөрчилдөж байвал тэмдэг, тэмдэглэлийн заалтыг баримтална.</br>

7.4. Замын тухайн хэсэгт байнгын тэмдгээс зөрүү утга бүхий түр тэмдэг тавьсан байвал түр тэмдгийн заалтыг баримтална. Түр тэмдэг нь зөөврийн суурьтай байх буюу энэ дүрмийн 1-р хавсралтын 1.6, 2.9, 3.3-т заасны дагуу шар өнгийн дэвсгэртэй байж болно.</br>

7.5. Замын тухайн хэсэгт байрлуулсан тэмдэг, тэмдэглэлийн утга хоорондоо зөрж байвал тэмдгийн заалтыг баримтална. Мөн байнгын тэмдэглэлээс зөрүү утга бүхий түр тэмдэглэл тавьсан байвал түр тэмдэглэлийн заалтыг баримтална. Түр тэмдэглэл нь улбар шар өнгөтэй байна.</br>
